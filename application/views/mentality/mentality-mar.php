						<h2>NASSIZU MURUME <br /><small>Zero to Hero</small></h2>
						<p><em>Story by : Warren Ligare</em></p>
					</header>


					<div class="row">
						<div class="col-lg-4">
							<center><img src="<?php echo base_url('images/'); ?>nassizu-murume.jpg" alt="Nassizu Murume"></center>
						</div>
						<div class="col-lg-8">
							<p><span id="quiz">PMA:</span> <b>Where or when did it all start for Nassizu Murume?</b></p>
							<p><span id="ans">Nassizu:</span> it all started as a joke then people started telling me I had a talent. I used to listen to Mr. Nice tracks a lot as a kid and I could sing along to them so here and there I would save up a couple of coins and record songs. </p>
							<p><span id="quiz">PMA:</span> <b>What challenges did you face starting out? </b></p>
							<p><span id="ans">Nassizu:</span> waah! Wasee kubelieve an Amerucan can sing was a hard nut to crack . Also when I was starting, getting funds to record music was hard since I didn't have a stable job. I have done so many pro-bono show since my music wasn't popular enough. </p>
							<p><span id="quiz">PMA:</span><b> When did your breakthrough come?</b> </p>
							<p><span id="ans">Nassizu:</span> My breakthrough came after I performed on Churchill show in Meru. When the show was aired on TV everything change, my fan base increased and social media blew up. </p>
						</div>
					</div>
					<p><span id="quiz">PMA:</span> <b>How did you get that opportunity?</b></p>
					<p><span id="ans">Nassizu:</span> Actually I used to work as matatu conductor so by the end of my shift I went for the audition but unfortunately they had already finished.  To console myself I began singing and by the time I was done,  I was on Churchill show. </p>
					<p><span id="quiz">PMA:</span> <b>what blessings have come out of that opportunity? </b></p>
					<p><span id="ans">Nassizu:</span> I am now signed to Watanashati Classics Management which also manages Keifa, Master J and Alex the big Boss.  I have also recorded ‘mawazo’ which  has become a big hit. Thanks to 'Mawazo,' I will be doing an eight state your in the U.S and I am also the brand ambassador of 8 vodka.</p>
					<p><span id="quiz">PMA:</span> <b>It is now from Meru to the world?</b></p>
					<p><span id="ans">Nassizu:</span> Yeah, thanks to hard work and God. You can make it from anywhere…you can be a star from Samburu like Dufla Diligon. The secret is, do what you are supposed to do and share with the world. Usirecord ngoma Alafu uanze kuskiza na prayer partner wako bedroom! </p>
					<p><span id="quiz">PMA:</span><b> What’s your take on the ‘you can only make it from Nairobi’ mentality?</b></p>
					<p><span id="ans">Nassizu:</span> Sio lazima ukue umetoka Nairobi ndio umake it kama msaani. I made it from Meru na pia wewe you can make it from wherever you are, just believe in yourself. </p>
					<p><span id="quiz">PMA:</span><b> Where do we now see Nassizu Murume in 5 years?</b></p>
					<p><span id="ans">Nassizu:</span> As of now am grateful to God because my music is doing well,  I am signed under a label and I have endorsements.  But in future I am working towards becoming the continental and eventually the flagbearer of Kenyan music.</p>
					<p><span id="quiz">PMA:</span><b> Which artists inspire Nassizu Murumu?</b></p>
					<p><span id="ans">Nassizu:</span> Mmmmmmh, many artists inspire me but I love Bollywood a lot. My role model is Shahrukh Khan. Closer home I love Sauti Sol, Alikiba, Diamond and Chameleon.</p>
					<p><span id="quiz">PMA:</span> <b>Any  collabos we should look forward to?</b></p>
					<p><span id="ans">Nassizu:</span> We are planning something with Pilipili and Khaligraph but ain’t doing collabos with big artists to get a breakthrough.</p>
					<p><span id="quiz">PMA:</span> <b>What is your final word to artists who are yet to make it in the industry?</b></p>
					<p><span id="ans">Nassizu:</span> There is no shortcut of making it and nothing is impossible but hardwork and prayer will take you all the way. </p>
			
