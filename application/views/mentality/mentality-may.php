						<h2>OTILE BROWN <br /><small>Like a phoenix, still rising</small></h2>
						<p><em>Story by : Warren Ligare</em></p>
					</header>
					
					<div class="row">
						<div class="col-lg-5">
							<center><img src="<?php echo base_url('images/'); ?>otile.jpg" alt="Otile Brown"></center>
						</div>
						<div class="col-lg-7">
							<p><span id="quiz">PMA: </span><b>You have gone through much, and still managed to release a new track ‘Yule Mbaya’ what’s the song about? </b></p>
							<p><span id="ans">OTILE: </span>‘Yule Mbaya’ is a typical societal depiction of how love sometimes hurts. It highlights the predicaments of a man in love with a woman who doesn’t appreciate the worth of the love the man has for her. Naturally, love is what conquers all and despite all the heartache, pain and emotional distress the man goes through, he sticks by the lady he lovesthrough thick and thin always hoping that tomorrow will be better.</p>

							<p><span id="quiz">PMA: </span><b>Your new song has been received well by fans and is enjoying massive airplay what do you have to say to that?</b></p>
							<p><span id="ans">OTILE: </span>People were anticipating my next move after parting ways with dreamland music. Once I released ‘Yule Mbaya’, the love and support I have received from my fans, friends and family has been overwhelming. I opened a new you tube account and uploaded my new single there and already it has over two thousand subscriptions.</p>

							
						</div>
					</div>

					<p><span id="quiz">PMA: </span><b>Where do you draw the inspiration for this single from?</b></p>
					<p><span id="ans">OTILE: </span>The inspiration for my music cuts across the board. Either from the experience of my fans or my own. Most of the time, its always women suffering in love but from my interaction with people I realized men also suffer in silence and so I decided to pen down their unseen pain.</p>
					

					<p><span id="quiz">PMA: </span><b>After the success of Yule Mbaya what next for Otile Brown musically?</b> </p>
					<p><span id="ans">OTILE: </span>Nothing but good music, and great collaboration with celebrated artists. For my upcoming projects, I’ll be switching from my comfort zone. My fans should expect club and dance music from me henceforth.</p>

					<p><span id="quiz">PMA: </span><b>Many young people after hitting setbacks like you did always give up and let go, how would you advice them?</b></p>
					<p><span id="ans">OTILE: </span>Own your hustle. Protect it with all your might. Always fight for what you believe in and hold onto your dreams. Irrespective of the circumstances, never let go!</p>

					<p><span id="quiz">PMA: </span><b>Word of advice to the younger generation of musicians when it comes to signing contracts with labels?</b>  </p>
					<p><span id="ans">OTILE: </span>Take your time and go through the contract with your lawyer but the most important thing is , make sure the Label or management has enough resources to facilitate whatever  is stated in the contract so that they don't waste your time or derail your plans.</p>

					<p><span id="quiz">PMA: </span><b>The few collaborations you’ve done have been a huge success any more we should look forward to?</b></p>
					<p><span id="ans">OTILE: </span>Yes, I have been working round the clock with artists that have received both continental and international accolades and when all is set I will begin releasing those projects to my fans.</p>

					<p><span id="quiz">PMA: </span><b>Which artists inspire the growth of Otile Brown musically and personally?</b></p>
					<p><span id="ans">OTILE: </span>: I get inspired by any good artist be it an upcoming  or a super star .I listen to many artist but am a big fan of Fally Ipupa. I wish I could dance like him though hahaha!</p>

					<p><span id="quiz">PMA: </span><b>How did you identify your unique sound that sets you apart and managed to stick by it?</b></p>
					<p><span id="ans">OTILE: </span>My style of music is calledkunidzia, famously known as bongo flavor. Having a coastal background myself, I found that genre resonating well with my inner being. With music being what I live for, every time I make music I do it passionately and whole heartedly and the reception so far keeps me going.</p>

					<p><span id="quiz">PMA: </span><b>Since you are famously referred to as the Kenyan Ali kiba due to your physical resemblance and vocal prowess when is the collaboration dropping?</b></p>
					<p><span id="ans">OTILE: </span>Very soon In Sha Allah! He's a great artist. His vocal ability, lyrical prowess and stage presence makes him a wonder. I would love to work with him and plans are underway. All my fans should wait on it.</p>


