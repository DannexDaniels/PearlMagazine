						<h2>Is the Internet Dumbing You Down?</h2>
						<p><em>Story By: Hope Ong'uti</em></p>
					</header>


					<div class="row">
						<div class="col-md-5">
							<p>Intelligence is the capacity to comprehend and to handle issues in a smart way. Google, among many other search engines, don’t offer you a way of thought but a massive ocean of information, and how you take it, use it and react to it is up to you. But could it be that the internet is thinking for us, or perhaps fashioning how we think? It is clear that our minds have the capacity to adapt but are they now made to laze in the process of thought, or, are they empowered by it?</p>
							<p>Google Map’s direction feature can dumb you down, as you don’t elaborately think a way out, your adventurous brain is shut down to follow pre-provided ways. There is no doubt on its proficiency and usefulness when you are in need of directions but an article on proper ways to follow, and environments to avoid on your way to a foreign place and perhaps the best all-terrain vehicles for road trips in the wild, would not think for you but instead offer you a myriad of options with which you will make your own perceptions. Think of the internet as the most normal calculator that should do the most basic operations, not the entire math.</p>

						</div>
						<div class="col-md-7">
							<center><img src="<?php echo base_url('images/'); ?>july_mentality1.jpg" alt="internet 1"></center>

						</div>
					</div>

					<p>We can assume that our intelligence is hampered when all we require is merely handed to us without the former strains of ‘hunting’ for it if I may. Thus, we would have a somewhat mentally slowing effect on the masses.</p>
					<p>What indeed is really going on here is our own behavioural and psychological reactions to stimuli such as an overload or a great ease in the acquisition of information. The most a piece of information can do to you is to stir up an idea, which you will adhere to, however, you shall. Unless there is some kind of information with an automatic hypnotic effect that will cause some unintentional action in you, which we certainly can’t say exists.</p>

					<div class="row">
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>july_mentality2.jpg" alt="internet 2"></center>
						</div>
						<div class="col-md-7">
							<p>Say, the governments found a way to make all food completely free and accessible to all in the whole world. As a matter of fact, people would fall ill, obesity shall hike, many who are driven to work in order to provide or sustain themselves with a meal, in fact, will not do that anymore and as a result, productivity shall be affected. Is this government now bad, for making food free and accessible to all without the taxing toil? By all means no! The obvious disadvantages that are underhand are not with effect because food is a killer or some elusive masqueraded destroyer, all the wrong that happens is as a result of the people’s own reactions to free food including their good or bad eating habits and their efficient workout programs or complete lack of any, along with their patterns of life regarding food to them.</p>
							<p>When the internet is used to spread pointless matters and rumors, it is us who haven’t learned to calm our nerves down and be at peace with the information era. We haven’t learned to slow down and take only that which we can and that which we need in an elaborate way. We have then made a mess of ourselves by failing to correctly pace and do good filtering in the mad vastness that we now have.</p>
						</div>
					</div>
					<p>So, the internet is not an openly bad or good thing. It is in itself a tool and how you use it provides the real meaning. What we are really saying here is that blaming our thinking on the internet; is virtually the same as blaming our poor choices in lifestyle on the availability of food. There is a massive bank of information, it does not think for you, you do.</p>


