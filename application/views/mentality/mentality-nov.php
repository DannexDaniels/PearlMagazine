            <h2>Who's the Man</h2>
            <p><em>Story By: Hope Ong'uti</em></p>
          </header>

          <div>
            <img src="<?php echo base_url('images/'); ?>nov-mentality4.jpg" alt="lonely guy">
          </div>
          
          <p>I know, I know, the right kind of guy, that guy the ladies want to have, that guy, the guys want to be. We must have them tall, muscular, sexy, and not to forget, the lovely eyes that tear into any person’s soul. Oh, dreamy. The perfect guy definition, what would that be, really? Would we define this by complexion, physique, intelligence, wisdom, personality, handedness, or perhaps there is a link to all these qualities of ours.</p>
          <p>Our society, speaking for my society rather, has a mental conditioning, such that men are supposed to be hard as rocks, impervious to emotional 'weakness', incapable of indulging in puny things like feelings. Unmoved by the formless nature of affection. Uncomprehending in the presence of the sympathetic. A display of feelings is irrational, abominable and out of question, and must be shut off at all costs. We have been taught that these 'weaknesses' are for the fairer gender, and now, it is only normal to stereotype women as weak, just because they are not exactly like rocks.</p>
          <p>Pent-up sadness and hurt eventually blow up as manly rage, with an inability to deal with matters of the heart, supporting further the assumption that men are without the capacity to feel, and women, who embrace these things, unable to think.</p>
          <p>No one denies that being macho is indeed attractive, but it is one thing to admire a robot you don’t live with, a different thing to live with an inhumane robot your own self, and a whole new scope to be this dreadful robot.</p>
          <p>Before men were, they began as boys, and before boys, babies. A conditioning, a master sieve, filtered what men were supposed to be like. This allegorical sieve removed the impurities that dirtied the qualities of a proper man. The ability to care deeply got replaced with the superior ability to manhandle whatever caused a disturbance, yes, from human beings to animals, to inanimate objects. The ability to think diversely got replaced with the finer ability to think in a straight line and perceiving nothing else.</p>
          <div class="row">
            <div class="col-md-8">
              <p>It is the society. It taught men it is a sin to be in touch with themselves. It equated compassion exclusively to womanhood. Tears were unheard of from a man like the glands were there by some mistake.</p>
              <p>It is not that men stopped to feel, it is that they lost any person that would not judge them to be lesser men as a result of doing so. The only remaining emotion worth embracing was anger. Anger was versatile. It is the only emotion that could represent all the other emotions. When you ought to feel sorry, anger saved you. When you ought to show affection, you remember your stern uncles and get angry instead. Any dissatisfaction is pent up and eventually explodes, the more erratic the misguided notion that men are without feeling spreads.</p>
              <p>It is only when the society drops its misleading concepts that men can learn to be whole human beings. It is then that they can learn that being compassionate is a trait of a good human being, not a woman.</p>
            </div>
            <div class="col-md-4">
              <img src="<?php echo base_url('images/'); ?>nov-mentality.jpg" alt="an angry man">
            </div>
          </div>
          
          
          <p>The real man would then be the one that has learned to be authentic with himself, unafraid of the societal constructs, unshaken by media propaganda on what manhood is and what it isn’t. To assume there are certain things for a man that need to be constrained is to say he was created with error, and equally is to disdain the magnificence of the universe and its grand nature.</p>
          <p>It is okay for men to cry, yes men can cry, and let out any kind of stress while dealing with their demanding standards, without needing to feel like clenching a fist or feeling alienated in a macho prison. It is okay for men to say they are hurt, without fearing they would seem like a woman, besides, what is bad with being like a woman if it is virtue and honesty that is on the line?</p>
          <div class="row">
            <div class="col-md-5">
              <img src="<?php echo base_url('images/'); ?>nov-mentality5.jpg" alt="sad and lonely man">
            </div>
            <div class="col-md-7">
              <p>The man has been curled into a corner where the only other human being that can understand him is his beer bottle. With a sip then a gulp friendly beer relates. A beautiful drink, a beautiful human who permits you to vent freely, who allows you to feel hurt and express truthfully on real issues of life. Friendly beer allows you to mourn even beyond the confines of a lost soccer or rugby game. For no one else permits men to feel or to think freely, dear beer shall listen. For no one permits weakness, even human weakness, then even prostitutes shall do, just pay up and you will be taken just as you are.</p>
              <p>My strong man, with whom all want to lean on and be heard, who shall hear you? Shall you lean on your own shoulder? Aren’t you human after all? Who shall listen to the heart no one thinks you should have? Who shall attend to the wounds of your soul when to them a rock cannot be harmed? Who shall stop the flow of your blood since to them you run on fuel? Who shall hug your secret, confined, aching self, perhaps even secret to your own self?</p>
            </div>
          </div>
          
          
          <p>A true man is he that is in touch with himself. One honest with himself concerning how he thinks, how he feels and why. Who is well-rounded and developed as a person. Who is not shaped by false theories, who is unafraid to be unregretfully their deepest, truest self.</p>

