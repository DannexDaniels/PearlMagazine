            <h2>THE EXPENSIVE COST OF FREEBIES</h2>
            <p><em>Story By: Tyler Leteipan</em></p>
          </header>
          
          <div class="row">
            <div class="col-md-7">
              <p><strong>If you love someone, set them free. If they come back they're yours; if they don't they never were!</strong></p>
              <p>When I thought my year would just be another year, something out of the ordinary happened. I met X. He was this amazing but weird kid that I couldn’t wrap my head around. He was mystery all over, and that’s what intrigued me about him.</p>
              <p>He was the type that was disconnected from his phone. I mean, which millennial doesn’t scroll through their phone every five seconds to check their WhatsApp status? When we first had a real conversation, he asked, “Who are you when everything else you identify with has been taken away from you?” Bingo! There and then I knew this was a smart kid that I wanted to be around.</p>
            </div>
            <div class="col-md-5">
              <center><img src="<?php echo base_url('images/'); ?>dec-mentality.jpg" alt="hearts"></center>
            </div>
          </div>
          
          <p>The most shocking revelation came when we had a fall out, (story for another day). He said, the ‘favor’ he did for me was only because we were friends! Now that we were no longer friends, he couldn’t do it. I did not get it. Not that I get it now, I still don’t get it. I never thought such people existed. I was the last one of that specie and that part of me died a year ago.</p>
          <p>I mean to meet a guy who subscribes to the philosophy of philanthropy? How nice, how kind. I didn’t want to lose the favor he was doing me. Being the master of art I am, I got my clown nose and wore my actor’s boots. You want to play, let’s play. Things have been smooth since then.</p>
          <div class="row">
            <div class="col-md-5">
              <center><img src="<?php echo base_url('images/'); ?>dec-mentality3.jpg" alt="hand in chains"></center>
            </div>
            <div class="col-md-7">
              <p>However, the other day I had a sit down with my dad. I told him of the rare philanthropist I had bumped into. In Swahili they say, “Mzee hakosei,” my dad’s thoughts just gave me a new realization. He said, baby girl, nothing is for free, even freedom is not free. The “I only do it for you sentiment is bull. There are always strings attached…just that the strings are invisible.”</p>
              <p>“He pissed you off. Naturally you need time to get over that. He knows that you will distance yourself and he uses his ‘favor’ to arm twist you back to the table and force you into a deal. You sign the deal with a smiling face and a bleeding heart. For how long will you put up the show?”</p>
              <p>I put up the show pretty well, until the Amadioha gods begun doing an angry chant in me. Is there any other person that can do what he was doing for me? Can I do it myself? Am I ready to forgive and forget? Then I said, to hell. I will do it myself. Then, I don’t have the burden of pretense on my shoulder. Maybe one day we will reconcile and I will go back to enjoying the favor but today and tomorrow, no fam! I will do it myself and enjoy my freedom. At least when I flash my teeth, it will be to dig into his flesh, not a fake smile.</p>
            </div>
