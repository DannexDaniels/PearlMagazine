            <h2>Valentines Day</h2>
            <p><em>Story by : Warren Ligare</em></p>
          </header>
          
          <div class="row">
            <div class="col-lg-6">
              <span id="ans">What is valentine’s according to you?</span>
              <p>It is a time to appreciate loved ones and spread love all over the place to anyone and everyone. It’s just a time to show love, love and be loved.</p>
              <span id="ans">How do the media portray Valentine’s Day vs. reality?</span>
              <p>It has been painted as project exploit men, where women expect to be pampered and treated by men to exquisite places and lavish gifts but the reality on the ground is valentines is a time to try and outdo each other with love and generosity.</p>
              <span id="ans">Is Valentine’s Day overrated or underrated?</span>
              <p>It depends on an individual. If it’s a man who shy’s away from responsibility then to him it will be overrated because he has to spend his money on a course he does not consider ‘noble’ but if it’s a man who understands the true meaning of love and appreciates it then he will definitely see it as underrated day because he wishes everyday would be valentines so that all he can do is love.</p>

            </div>
            <div class="col-lg-6">
              <img src="<?php echo base_url('images/'); ?>mentality.jpg" alt="Valentine’s day">
            </div>
          </div>
          <span id="ans">Should valentines treat be a two way street?</span>
          <p>Yes of course everyone enjoys being loved and appreciated so while your man is busy taking you out and showering you with gifts a little treat from you will also go a long way in showing him how much he means to you.</p>
          <span id="ans">How would you spend that day?</span>
          <p>I would have long endless and interesting conversations with her. We would have a three years conversation in a day just motivating and inspiring each other.</p>
          <span id="ans">If you had a million bob to spend on valentine’s what would you do?</span>
          <p>Invest it so that I get to live every other day like valentines spoiling her with whatever her heart desires.</p>

