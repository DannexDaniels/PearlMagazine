							<p>When the list of BBC top 100 most inspirational and influential women came out, three Kenyan girls had made it to the list. One of the amazing girls was Naomi Mwaura. Naturally when you meet her she is such a quiet and gentle soul. Do not be deceived, when a course is close to her heart she turns into a lioness with a soul on fire.</p>
							<p>Though making it to the list is a big achievement, this humble girl did not even expect it and does not look at it that way. “It was an emotional moment; I called my mom to share with her the good news. But, that win was not just mine. It was for each and everyone of the thirteen girls that had stood by my side when we staged a peaceful demonstration to condemn harassment of women in public places. It was for everyone who had walked the journey with me towards ensuring public spaces are safe for women.”</p>
							<p>Together with her 3 friends, she started an organization named <strong>FloNe initiative</strong>, to address the social injustices in the society and push for social reforms. Naomi was no longer just an angry girl; she was a girl on a mission. “I was raised in a family that did matatu business. It’s that business that schooled me and made me who I am. Where was the discord? I wondered. The matatu operators, both women and I would rub shoulders out there which were totally different from the ones I interacted with at home. Then I figured it was probably because these people did not have information, they did not know how to run the business they were in. So I took it up to myself to create awareness.”</p>
						</div>
					</div>
					
					<p>Naomi identifies areas prone to violence and does training there. “These people are not taught how to operate customer centered businesses. All we do is just give them matatus and expect them to bring in cash in the evening. We are seeking to fill the gap by raising their awareness to all types of violence against women. Be it verbal, physical or emotional. We teach them how to draw boundaries and what to do when they find themselves in tricky situations. This way, we found out that they are not bad people but just ignorant of their surroundings.  After enlightening them, we have seen a reduction in cases of abuse of women in public places.”</p>
					<p>Some of these behaviors perpetrated by men she says, have been bred and groomed by the society. As a people we have mastered the art of victimizing women and christening men. But if we are to witness change, we as a society need to check ourselves. “Violence is based on the way we socialize, what we say and laugh about as if it’s normal yet its not, attitude towards very important things such as respect, and the perception that women are sexual beings and nothing more. By undressing a woman in public, you are punishing her for your thoughts. You think she dressed to turn you on but she just dressed to feel good about herself. The chances that she even knows you are zero. In our awareness classes and training programs we try to address such stereotypes as a way of trying to deal with the problem.”</p>

					<div class="row">
						<div class="col-md-7">
							<p>Among other things that FloNe Initiative does is raising awareness around reproductive health rights. “Women need to start taking charge of their sexuality. Sexuality imparts many things. If you don’t take charge of it, you are at risk of getting an STI or an STD. We tell women its okay to bargain for a condom during intercourse until they have gone for tests with their sexual partner. We think that your health is a way huge responsibility to leave it at the mercy of anyone. Attending vagina monologues while still at University and even going ahead to host four in schools, my eyes opened up to women and sexuality and it made me realize many women have not owned their sexuality let alone even being aware of it. Ranging from pleasure, to contraceptives to vaginal health…we encourage women to have conversations about these things publicly and be as blunt as possible.”</p>
							<p>Gender equality is another important subject that is close to Naomi’s heart. According to her, empowering both genders is the only way forward. “What we are doing is we are just levelling the playing field. We are getting a lot of resistance, but from those who were having it easy when gravity favored them. When we empower women, we all feel the ripple effect. For example, if women can own land, this means they can take up loans, they can invest, do business and this money trickles down to the whole society. Nobody should feel threatened by a female revolution. If you ask me, gender is based on our anatomy; all these other tags attached to gender are man made, flexible and negotiable."</p>
						</div>
						<div class="col-md-5">
							<br /><br />
							<img src="<?php echo base_url('images/'); ?>nov-mainstory4.jpg" alt="Naomi talking to other women">
						</div>
					</div>

					
					
					<p>Every single day, Naomi is pushing for social reforms. She chooses to look at the bigger picture and keeps pushing. “Public transport systems are our main access to mobility as a country. Mobility affects access to jobs, education, healthcare and even a social life. If public transport and it’ operators make people feel unsafe, they will opt to buy their own cars. The sustainability of private cars is an uphill task due to congestion and pollution. If we train matatu operators and make people feel safe, they will probably invest the money that was to be used to buy a car into other ventures and this will eventually lead to employment, productivity, increased national income and eventually economic growth. “</p>
					<p>This road has not been an easy one. It’s a tough terrain in a stormy weather. “I remember while organizing one of our demos, there was another anti demo group that came to attack us. We were lucky to have hired enough plain clothed police to protect us, although the bills weighed heavy on my shoulders. The road to social justice and reform is quite a lonely one. Not many people have taken it up and I end up feeling lonely at times. Also, the frustration of what you go through or what the people you help go through sometimes gets a better part of you and you need to be careful not to get consumed by the evil you get to fight against.”</p>
					<p>There have been good days too. Days that make her feel all the trouble she goes through is worth her time. “The feedback we get after every session of testimonials makes my heart swell with joy and pride. Previously, our sessions were meant to hold 35 people but we have now been forced to expand and train 43 per session. We have also started the ‘women in public transport’ training program, and to see women from places like Mwiki come to our session and enjoy the learning process means the world to me. </p>
					<p>Well if you think this amazing girl is done, you are mistaken. This is just but the beginning for her. “I would like to increase the impact and reach of our organization and training programs. I would the government to come on board and be a part of the training by ensuring it’s a nationwide project. So far so good, but till all public transport operators get to understand what violence is, and how to deal with it, and all women feel safe, we, both you and I still have a job to do!</p>

