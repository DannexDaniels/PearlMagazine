						<div class="col-md-7">
							<p>Every time you see her on the screens, she’s mostly dressed in hues of blue, her favorite color. She is super stylish, with a warm smile and bubbly personality. She has come from far, deep down she is a fighter. Her whole life has been a battle field and this girl still has her gloves on and is standing tall in the ring. She is not afraid of rematches, after loosing balance she braces herself for impact determined to remain standing afterwards.</p>
							<p>While on my way to do this interview, I make a list of normal questions for the interview, little did I know it was going to be a deep conversation filled with tears (I cried all through while she smiled) and self reflection. Dressed in her favorite color and wonderful blush that stands out beautifully, she smiles and we chime into the conversation.</p>
							<p>She got diagnosed with liver cancer in 2013. It has been a tough journey but she has learnt to embrace the journey and learn from it. “According to Louis Hays, liver cancer is as a result of bottled up emotion and anger. It’s a consequence of peented emotions. I got diagnosed with liver cancer in 2013, it has been a battle of pain, loss, finding my footing again and growth. I had pilled up so much in me and it affected me. The cancer was a wake up call for me to do a lot of forgiving, forgetting and letting go. Through the whole process to date I have learnt invaluable lessons about life,” she says.</p>
						</div>
					</div>

					<p>Anjlee has gone through so much pain through her journey. To her, pain is a shift in the state of mind. It teaches you to vie life differently. It opens up your consciousness to things that you might have taken for granted. “I have gone through so much physical and emotional pain through this whole process. From the transplant to the infections that came about due to surgery. I was receiving my treatment in India away from home and my children. The chemotherapy was no joke. Through the pain, I found out how resilient I was. There was innate and infinite strength that I discovered. After the surgery, I went through so much pain and I thought I was going to die but here I am. I have also learnt that pain teaches you a lesson. If you refuse to learn the lesson, you will find yourself in a more painful situation until you learn whatever it is the universe intended to teach you.”</p>
					<p>In the beginning the cancer took a toll on her. She almost lost her balance. To her, its okay to feel lost in a tough situation. From loosing yourself, you strive to find yourself and your way. Henceforth, your life will have a sense of direction. “I went into a state of depression and even hate and anger. But I have learnt that I am in control; total control of my body. Once you realize you that you can take charge. You have the power to tell your body to take a break and think things through. You have the power to decide what you want or who you want to be. Stay positive, and ensure you are in a positive mindset. If you control that, nothing around you will break you.”</p>
					

					<div class="row">
						<div class="col-md-7">
							<p>Many people feel like a death bell has been rung when tragedy strikes. Instead of taking a moment to reflect and plan on how to tackle the problem, we probably shift to worst case scenario mindset and slowly we begin crushing. “If you call out for help it will come to you. The definite thing is that most of the time we just don’t realize it. When tragedy strikes we shift into panic mood, STOP. For you its cancer, another is HIV and AIDS, another is a rocky marriage, financial crisis amongst others. Each one of us is going through something. Appreciate your journey and embrace it.”</p>
							<p>One might wonder how some people do it. They have the ability to rise up despite the heavy burden trying to pull them down. On the contrary, you feel tired, every time you try pushing you feel like you are crushing in the process. Anjlee has been there. “There is an innate ability in every single person to survive any experience they go through in life. God will not give you something you can’t handle. It all happens for a reason. You’ll find strength while going through it. You learn from the pain, the tears, the sweat and the blood. You learn how to be patient and appreciate your own journey. The tears wash your eyes for a clear vision and gives you a sense of direction,” she says.</p>
							<p>Anjlee feels like in this current day and age, life has taken over people rather than people taking charge over life. Who are you? We live in a world of fake imperfection and we enjoy it forgetting that the body is just flesh, it’s a vehicle. On the outside we flourish, but in the inside we are empty shells. That is why many people come down crushing at a high speed. “I have found myself deep within. I have disengaged myself from my body and I have focused on growing and feeding my soul. Thanks to social media, we have become selfish. We live in a bubble, chasing perfection. We all want big cars, big houses and we have forgotten about soul food. I found a pranic healing school in the midst of my confusion and I have found my self. It’s a process that helps you heal yourself from deep down. You look deep down in you and ask yourself what is it that I am going through, why am I going through it and what can I learn from it? Make it a reflective process and learn from it.”</p>
							<p>A testing time reveals the true colors of people. You get to find out who are your genuine friends and who are not. Along the way, it’s natural for you to grow and change and this change also prompts change in regards to those you relate with. “Its natural that when you start a journey you have a whole pack behind you, but along the journey and even towards the end you don’t necessarily finish with those people. The universe is well structured, everything happens strategically. You need to ask yourself in your current situation; what is the role of those who surround you? Be selective of who you let in. try and pick out qualities in an individual. If it is negative and toxic, cut it off.”</p>
						</div>
