					<p>This brave girl has come to appreciate that doctors and medicine can do so much but true healing begins with oneself and is done by one. You have the ability to bring back your health be it mentally, spiritually, physically and even emotionally. “Sadhana: service. How do you learn to appreciate yourself and life? When you are able to meet the less fortune and leave a positive impact in life. Master Chao says 10% tithe, 20% save the rest spend. Whatever you sow you reap. Service brings a reflective sense of one self. Spend time in places with divine outpour. That is a positive energy you are channeling into yourself. Go to places where there is positive energy you can tap into. That is how healing begin. But the primary healing step is forgiveness. Learn to forgive yourself and those that hurt you and forget and genuinely bless people. It’s a full thorough detox in your system.”</p>

