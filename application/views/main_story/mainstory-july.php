							<p>One might help but wonder how did Anjlee deal with the fear of loosing her life and even her two beautiful daughters? According to her, fear steals your life away, face your fear and live each day to the fullest. “I have fears too. I have lived in fear but I have conquered my fear because nobody knows tomorrow. The odds are way beyond our comprehension. Death is creepy and in an eye blink everything can be taken away from you. A person battling cancer will have everyone thinking they ate dead and a perfectly healthy normal individual passes on leaving the cancer patient fighting on. What are the odds? I lost my health, my life and wealth. I had lost peace. To break that vicious cycle took a lot of pain to realize am on a different path. I stopped comparing myself to others and my journey begun unfolding itself to me and everything made sense to me. I am no longer scared of death, I make the best out of everyday, trying to serve my purpose. To me, death is an opportunity to evolve, a time for my soul to get elevated to a better place.”</p>
							<p>After every battle there is a scar to remind you of what you went through but came out alive. Many people have scars that they are ashamed of, but not Anjlee.  “I have scars all over my body. They tell my story of how much of a fighter I am; how I have conquered everything that has been thrown my direction. There was a time I was conscious and covered them. I have gone through over a total of seven surgeries. It just hit me that I need to stop being conscious about the scars because nobody will notice. I have learnt to wear them with the same grace I wear my clothes with. Wear your scars with confidence, you know what happened to you, don’t hide it and embrace your journey.”</p>
						</div>
					</div>
					
					<p>As a mom that has been through hell and back, her perception on parenting has changed. “I hope my daughters grow up and make a difference in a society. I hope up they grow up having each others back growing through life knowing they have each other. I love my daughters and I pray I see them grow into women of dignity and honor.”</p>
					<p>Anjlee has learnt to live each day like its golden. She has mastered the art of self love and appreciation. “Every time I look into the mirror, I am reminded that I am a strong beautiful child of God, divinely protected and divinely loved.”</p>

