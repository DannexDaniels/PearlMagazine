					<p>Many at times, it’s easy to let a friend run an errand for you and you trust them. How about total surrender to God? In tough times, instead of surrender many of us wear their boxing gloves and instead of fighting end up taking heavy blows. “Stop trying doing it on your own. Give it up to the universe to help. Let go and let God. It is very difficult to get to a state of vulnerability. It takes a crushed ego to let go. Most of the time we are proud and egocentric and enjoy being in control. Even when we are not, we try so hard. Until it slips through you, you’ll keep trying but the day it knocks you down and crushes you, you are left on your knees with nothing. That is when you look up, and call for help. You get purified, the pride and the ‘I’ mentality is gone, that is when help smiles your direction. </p>
					<p>When I was in India, I once went to the temple and I was tired; of chemotherapy, of being away from home, of fighting. I broke down and let go. Later on after about 3 hours of painful chemotherapy, I told my husband I wanted to go to Calcutta the next day. The moment I stepped on the pilgrims my pain vanished and henceforth none of my chemotherapy sessions was painful. I have made it a point to go to very spiritual places I can to ask for one thing; my health. It is God that gave me life, only Him can take it away from me, nothing else.”</p>
					<p>Cancer has opened Anjlees eyes to her surrounding and has taught her the art of gratitude. She has learnt to look at things with a price tag differently and learnt to attach value to priceless things. “Appreciate every single thing in life. Our gratitude is so little. We take so much for granted. Start projecting inner gratitude and everything disintegrates and there will be no room for darkness. It’s a brand new day, you’ve seen a sunset, you have shoes, clothes, food, shelter, family and perfect health. Stop attaching value to materialistic property. It will never be enough. It’s a constant chase that never ends. Start appreciating the priceless things. Life in itself and all it has to offer. I take my shoes off and enjoy stepping on the grass, opening the curtains in the morning and smiling at the sunrise. Know when to stop. Hustle will be there has been there. Hustle has stolen our lives…We are trapped in a golden cage of materialism. We feel trapped but truth is we are comfortable bit we have forgotten that the gate is right there in front of us. Take a moment laugh and enjoy life, stop setting standards for happiness, if you can’t laugh at a silly joke, if you can’t appreciate simplicity its time to check yourself and reflect.”</p>

