					<p>Amid trying to figure everything out, Mwende is still a girl whose eyebrows are always on fleek, lipstick on point and high heels. “It’s a journey. I am in pursuit of growth. A journey is laced with tears, pain, joy and laughter. Every day while you journey as a girl, ensure you spend your time with those worth it, chose your battles wisely, love yourself more than anything and always overflow with positivity.”</p>
					<p>As we celebrate mother’s day this month, we can’t help but celebrate Mwende who is also a mom to a handsome baby boy named Champion. “I became a mom in 2013, but I have always been a mom since I lost my mom at an early age and had to raise my siblings. Motherhood is a breathless experience. He’s so beautiful and he’s my son. It’s also breathless because its hectic, running out of breath chasing him, raising him, trying to do right by him but I’d never trade the experience for anything.”</p>
					<p>As we wind up the interview, Mwende is keen to advice the younger generation on chasing after success.  “I have learnt to be patient and allow God work things out at his right time. Nobody should fall prey to the temptation of shortcut. If you want it, work for it, fight for it, believe in it then patiently wait upon it. Things will eventually work out in the long run the way God intended it to.”</p>
