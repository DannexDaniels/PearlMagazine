						<div class="col-md-8">
							<p>She has branded herself double  M. Every Monday to Friday from 2pm to 4pm her bubbly voice and warm laughter takes over.  She has over the years taken over the airwaves storm and seats and the pinnacle of radio Queen ship. We connect with her on Radio Maisha through her show ‘Connect' , her name is Esther Mwende Macharia.</p>
							<p>Growing up, the limelight was always her fantasy. One day she always dreamed of being a super star. “KBC was the station to watch growing up. I would look at their news anchors and envy how pretty they looked. Every time I saw them I got more determined to get to where they were.” This marked the beginning of her ten years media career. </p>
							<p>After completing high school she begun covering events and writing articles there after joining college to professionally study journalism and later on doing her internship at Baraka FM where her breakthrough was awaiting her.“Being the warm and outgoing person that I am, personality emancipated me. When Antony Ndiema heard my voice, he liked it and with his help, I recorded my first ad and since then there has not been any turning back.”</p>
						</div>
					</div>
					
					
					
					<p>The idea of connect was borne out of of the loophole that was on radio. “I used to do Staarabika from 10am to 1pm. Because I was only 22 my bosses felt like the show was too mature for me since the audience age bracket of the show is 27-40. Also, after staarabika there was no show in between as the next show was an evening show and hence connect came to birth from 1pm to 4pm as a filler but is now rated the number one show in the station.”</p>
					
					<div class="row">
						<div class="col-md-8">
							<p>Getting connect to be a household name has been a rocky but a rewarding journey. “The show is designed for a particular type of market and that market was already deeply rooted in other shows. I had to put myself out there, attend events just to push the brand and make the market aware. The most painful bit of it was that my son Champion was barely a year old and ‘mama’ had to work overtime to ensure connect eventually becomes accepted.”</p>
							<p>The 21st century ironically brings with it the instant success generation. Nobody wants to go out of their way and feel pain before enjoying the pleasure of success. “Building brands take a lot of hard work and determination. It is not easy for someone to share in your vision let alone believe in it. I started radio in May 2009, and for someone to even be familiar with my name has taken a total of eight years to build. I have worked at a regional station before I even got a national station. Growth is gradual, It’s not instant.”</p>
							<p>The achievement of ‘Connect’ has even surpassed her imagination. “we have become a force to reckon with. Young people are listening and getting inspired by the day. We are creating and giving Kenyan music a platform, artists a voice and upcoming artists opportunities to share their talent with the world.”</p>
						</div>
						<div class="col-md-4">
							<img src="<?php echo base_url('images/'); ?>main6.jpg"  alt=" Esther Mwende 2">
						</div>
					</div>
					
					<p>Mwende is now using her success to uplift the younger generation. Her main intention is to see the youth fully empowered. “I run a foundation that is named after my son, the Champ Foundation. The Kenyan curriculum is specifically tailored to suit professionalism but doesn’t mold an individual into a whole package. Through our mantra Jijue,Jiamini, Jijenge, we visit high-schools and universities and offer guidance to these youngsters that are beginning to discover themselves. It’s a lot of fun, interaction and learning. Through personalities that they know and admire, we take to them advice, motivation and inspiration. Over the years, those who have passed through the champ foundation are grateful and have made something out of their lives. That is my joy. That is what gives me inner satisfaction.”</p>
					<p>With the desire to live her life in a society that judges anyone based on what they read on blogs, Mwende has learnt to find the equilibrium. “I am myself. I keep it real and stay true to myself. I keep my problems to myself and laugh a carefree laughter. I am no longer afraid of living my life because people will talk. I am living my life to the fullest.”</p>
					<p>The future for this radio queen is bright as she intends to conquer continental and eventually international airwaves. “I would love to host a show that transcends boarders. Trending at number one is Kenya is not enough; I would like to host a show that brings the whole continent together. Discuss issues that will lead us to progress and celebrate our very own. I also want Mwende as a brand to grow; in terms of being a radio host and also an MC.”</p>
