						<h2>NANCY IMALI GUANTAI <br /><small>AKA<br /> <b>FAYA MAMA</b></small></h2>
					</header>

					<div class="row">
				<div class="col-lg-4">
					<center><img src="<?php echo base_url('images/'); ?>faya-mama.jpg" alt="Faya Mama"></center>
				</div>
				<div class="col-lg-8" id="story">
					<p>
						<span>E</span>very day of the week from 1pm to 4pm, she is the voice that graces Mmust FM bringing her listeners a reggae extravaganza. Her playlist encompasses roots, riddims, ragga and dancehall. At 9:30 in the morning, she drives into our set cheerful as ever and apologetic that she is late for the shoot and interview that we had slated for 9.
					</p>
					<p>
						Her passion for reggae dates back to 1997 when she was in class six. During the holidays from 2pm to 4pm, she would tune into KBC to listen to a reggae show. "The show was always hosted by John Karani and Charity Karimi. I used to like how the two hosted the show together. To me, it was laid back type of music that had conscious messages that related well with the day today happenings of the society."
					</p>
					<p>
						Upon joining high school, the reggae fire in her died down because she lost contacts with mediums she could listen to the music from. The reggae gods smiled at her again when she was in form three. "I did home science and the home science lab had everything a bed and even a radio. Also, in the home science lot, i found my fellow reggae lovers and my passion was reignited all over again. During our free time, we would listen to reggae and share the history behind the song."
					</p>
					<p>
						For her tertiary level education, she proceeded to a Christian university and all she could play during her show was only but gospel music. But that did not kill her reggae vibe, it was sleeping deep down, awaiting to be poked. She did her attachment at KBC nothing much just attached to the business desk and later on in the PR department.
					</p>

				</div>
			</div>
                    <p>
                        Her breakthrough for reggae came when she got employment at the Masinde Muliro University radio station. "When i came went back on radio may 2012, i knew this was my opportunity to take my love for reggae to the next level. From May to December I was in charge of news but early 2013 I was given the opportunity to produce an afternoon show. The show would only play riddim, a genre popular with the youth and to me that was too much noise and so I rebranded the whole show." Being the only show that would entertain her listeners with reggae music she decided to bring back the cool conscious reggae music that has content that relate with a peoples day to days life and since that day there has been no turning back.
                    </p>
                    <p>
                        Her radio name faya mama came from a caller who was listening in on her show. "During the second week of doing the show, a caller called in and said to me mama you are on fire, why dont you call yourself Faya Mama, and upon giving it a little thought i settled for it since it resonated well with the show." Her Faya Mama brand has really grown since then and her territory keeps expanding by the day. "I began doing research and talking about real issues that affect people and played music that was in tandem with the topic of the day which created a better bond between me and my fans. Also, I nurture students who come for attachment at Mmust FM and have a passion for reggae music and my earlier students are doing well in the mainstream media."
                    </p>
			<div class="row">
				<div class="col-lg-4">
					<p>
						The irony of her story is she doesn’t look anything close to Faya mama, no dreadlocks, no Rasta Farian regalia. "Just like Morgan Heritage sang, you dont have to have dreads to be Rasta. My love for reggae is inbound and runs so deep. I love the music and have the history on fingertips unlike many who will portray reggae externally but have no basic knowledge about it." And unlike the prior perception that reggae music is associated with hooliganism, smoking bhang and theft, she says reggae music is all about peace and conscious messages and people should change their perception towards the music.
					</p>

				</div>
				<div class="col-lg-4">
                    <p>
                        While kenya does not have many reggae artists the few who have survived are doing well and people have begun warming up to it. " The promoters have realised that to restore dignity to reggae music unlike the old days when it was associated with holiganism, they are choosing good venues that ensure the atendees feel safe and the gate charges are able to sieve the serious true lover of reggae from the hooligans since one has to save up to be able to meet up the gate charges."
                    </p>
				</div>
				<div class="col-lg-4">
					<center><img src="<?php echo base_url('images/'); ?>faya-mama4.jpg" alt="Faya Mama"></center>
				</div>
			</div>
                    <p>
                        The future for Faya mama is bright as she hopes to be heard in a national platform where she can use the airtime to play good and conscious music and change the perception and attitude of people towards reggae music. To get there, she draws inspiration from people in the reggae industry. "I love Talia Oyando. She doesn’t use the reggae language or partois language to do her show but still does a good show. I also love Queen Ifrika and how blunt she is in her music she says it as it is and calls out moral filth and social injustices in the society.  Lucky Dube also plays a major role in my life since he was bold enough to stand up against apartheid and did music that anyone anywhere can relate with."
                    </p>
			<div class="row">
				<div class="col-lg-7">
					<p>
						Growing up, she travelled around the country since her parents were educationists. Growing up with three other siblings was difficult and fun at the same time. "We used to stay in a two roomed house in Kawangware with my parents. Times were not easy but they sacrificed and gave their all to give as quality education. There was a time i would look at my closet on Sunday when i was preparing to go to church, and I would burst into tears since my wardrobe had nothing worthy to wear. But as time passed by my parents got their breakthrough and Sundays became our favorite day of the week since after the service, our parents would take us out and buy us fries for lunch. At times, we would all squeeze in the car and set out for road trips, memories i can never trade for anything.
					</p>
					<p>
						Ironically she never thought she would be a journalist. Instead she wanted to be a medical practitioner. "Upon getting a B- I asked my dad to enroll me into Baraton University so that i could study nursing but he could hear none of it. So a family meeting was convened and after she had made her point on why she wanted to be a nurse, her aunt sealed the journalism deal by asking her if she had ever met a nurse as hyper as she is, and just like that daystar awaited her.
					</p>
				</div>
				<div class="col-lg-5">
					<center><img src="<?php echo base_url('images/'); ?>faya-mama2.jpg" alt="Faya Mama"></center>
				</div>
			</div>
			<div class="row">
				<div class="col-lg-4">
					<center><img src="<?php echo base_url('images/'); ?>faya-mama3.jpg" style="margin: 20px;"></center>
					<p>
						She did her three years at the main campus in Athi River but came to do her final semester from town campus. During a random day, she met a guy who seemed lost in the offices. The man was there to inquire about admission and being caring and active she helped the man out. The man was not just grateful for the help but grateful that he had found a lifetime helper and upon taking her number one coffee date led to another and today two beautiful boys Austin and Alveen are their mini trophies to show off.
					</p>
					<p>
						"Dating me was not easy. Since I was taking evening classes, my parents always expected me home by latest 9pm, so going out on dates was so much of a challenge but when i finished school it became easier. He won my heart over with 'Adhola' it was meat made from t bone eaten with chapatis. There is nothing more delicious than that. Dating him was fun and intense since both of us were serious and wanted to settle down since i had even indicated in my diary that by 27 i wanted to be married already."
					</p>
					
				</div>
				<div class="col-lg-4">
					<p>
						She speaks so fondly of her husband whom she fondly calls babe. During the interview, he easily finishes her sentences for her and help her say clearly what she is thinking. "He is a gentleman, though moving to Kakamega has made the public display of affection tone down since people around here are not used to that. He is caring and so loving. I remember while I was working at Presbyterian university on Valentine’s Day out of the blues he turned up with cake, chocolate and flowers and made my day beautiful. He is a fast and creative thinker and we think alike."
					</p>
					<p>
						For the six years they have been married, and just like any other married couple, they both admit that marriage is not easy. “Marriage entails two people with common feelings, common understanding and a mutual sense of respect for each other. These are the three pillars that will hold still the marriage when turbulence rocks. Marriage is not a bed of roses. People sometimes disagree to agree. He understands that am a journalist and is familiar with what my job entails. I remember sometime back when my son was barely 3 months old, I had to travel to Lesotho for a workshop and he allowed me to go and was even the one who went to process my visa."
					</p>
					<p>
						Being an inter tribe marriage she says the Bukusu culture where her mom comes from is so rigid that her aunties have never shaken hands with her husbands. Though her husband is an 'Amerucans' from Meru, she says she has blended well with his family and peacefully coexists with her in laws.
					</p>
				</div>
				<div class="col-lg-4">
					<p>
						She spends her free time with her boys trying to nurture the boys into future men of dignity and honor.  Courtesy of her hubby, she is a farmer on the side, with a farm full of goats, sheep and chicken layers. She says she enjoys farming since it’s a good course with good returns.
					</p>
					<p>
						As we wind up the interview. She advises the younger generation to live life as it unfolds itself. "We are not the same, our journeys have been tailored differently. Life takes its own course, today lady luck smiles at me tomorrow it smiles at you." She further warns the youth against the shortcut temptation and advises them to persevere and get it done the right way. And above all she advised anyone before anything to always put God first.
					</p>
