					<p>Startup capital is always a challenge to many people. Many have brilliant ideas but execution becomes a real problem since there is no money or there is not enough cash to start out. This is what Amani says, “Start where you are at. Build yourself up. The most important thing is progression. The little you have saved, start up. Don’t despise humble beginnings. They are your ladder up. Sometime we want a miraculous investor to invest in our business. But, we have to be accountable with the little we have so that we can be entrusted with more.”</p>
					<p>We live in an age where success stories are very inspirational but far from realistic to the common people. Comparison kills…do not compare your journey to another person’s journey. Instead, look for those who have walked the same journey as yours and find inspiration from their success story. “More and more stories of real startups need to be told. Stories of people who started from the ground with absolutely zero and have become heroes. I know a guy who has three boutiques in Westland’s. How did he start up? Selling mitumbas along the streets. Now he is inside malls with shops. I appreciate stories that use 3 million or 10 million start up because different businesses have different models. Identify which model works for you and start up right way.”</p>
					<div class="row">
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>dec-mainstory5.jpg" alt="Amani pic 9"></center>
