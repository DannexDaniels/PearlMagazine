						<div class="col-md-8">
							<p>A couple of minutes to twelve, a lady in jungle green water fall sweater, smiling walks in. Her hair almost made me green with envy. The good lady was kind enough to tell me she had complimented her natural hair with Marley hair from her diva hair collection. We sit down and make our order. She is the Amani you have seen on TV, but she is nowhere close to what you see or imagine. She is bigger and better. Ladies and gentlemen allow me to introduce you to the brand-new Amani.</p>
							<p>I quickly ask about her previous song bon, how the sound and even the feel of the song. The answer she gives changes the whole direction of our conversation. The old girl we knew, is no longer in her, there has been a rebirth. There is a new girl blossoming in her.  “Bon was the last one, while shooting it, I just knew it was going to be my last song. I just felt a conviction. I knew it was time to close that chapter. To leave it all behind and flip the page. A new lease of life.”</p>
							<p>Her journey through salvation did not start yesterday, she has always been saved. She walked away from all the fame and money easily without blinking. “I no longer felt comfortable in that environment. It wasn’t coming from a real place. I couldn’t fake it even if I tried. I went through a transition period. I went to Bible School to refresh my relationship with God. It was a process, even now it still is. Every single day God is working on something in my life. The Bible goes further to say, work on your salvation with fear and trembling. I am working on it. It’s not perfected in one day.”</p>
						</div>
					</div>
					
					<p>Amani for a long time kept her salvation private. This was to protect herself from all the pressure from outsiders that come with them knowing you have chosen to walk with God henceforth. “I don’t want to be put under a microscope, be scrutinized and condemned. I want to grow in my own space. I want to know God at my own time. I don’t want to post up a picture of me on Instagram smiling and get people saying there is nothing Godly about my smile. People are good at pointing fingers. Many people enjoy when you are wallowing in the same boat with them. When you leave, you burst their bubble and that becomes a problem.  They don’t have a person to compare themselves to and feel better about themselves anymore.”</p>
					<p>The journey has been a beautiful experience. Her life has changed. Her knowledge of God has increased 1000 folds. A new life has been unfolding right in front of her eyes and she is loving it. “I am a much happier person. Before I was playing an act, you have to act and behave like you are cool and maybe that’s not how you are feeling. Now, am at a more real place. God is revealing himself to me. The most important revelation is that human beings are multi-dimensional. Thing is, when I wasn’t born again, all I thought there was to me was music. I didn’t think about other things I could be. When I began knowing God, I realized the other passions that lie within me. Salvation pushed me to venture into other things that I had pushed aside in my life that I liked doing.”</p>

					<div class="row">
						<div class="col-md-7">
							<p>From passion, diva hair collection was born. A baby that is a year old. It’s taking slow but steady steps. Determined to grow and change the story of African women and their hair experience. “Hair was always my side hustle and I decided to make it my full-time job. The planning stage took 2 years.  I did my research and I strategized on how I would get into the market and curve my niche. I worked with a couple of stylists to get the market’s reaction of our product. The feedback gave me the green light. The market reception of my product has henceforth been amazing. Our clients keep coming back for more! </p>
							<p>Is there any personal attachment to the brand you might wonder? “Weaves and chemicals had yanked the back of my hair out. Here I am before a concert, tracking the back of my hair, leaving the front. I had a sore that wasn’t healing. I tried everything, it wasn’t working. My hair stylist suggested we try crochet. It wasn’t heavy on the scalp. It would give the wound time to heal and the scalp an allowance to breath. Crochet helped me grow my hair back and I wondered how many women had gone through the same or worse and needed help? We came up with the idea of crochet. Secondly, I wanted something I could blend with my natural hair. Problem is, the other braids are too silky, two weeks down the line, your natural hair is sticking out. So, we carefully selected the Marley braid that blends well with natural hair.”</p>
							
						</div>
