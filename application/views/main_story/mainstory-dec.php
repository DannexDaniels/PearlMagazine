						<h2>AMANI<br /><small>Brand New Me</small></h2>
						<p><em>Story By: Sibuor Madhako</em></p>
					</header>
					<p><strong>Just like a flower I have opened up. I am blossoming, for God’s plans for me are perfect. Even if I have to wait, I count it all joy because He will make the wait worth it!</strong></p>
					<div class="row">
						<div class="col-md-4">
