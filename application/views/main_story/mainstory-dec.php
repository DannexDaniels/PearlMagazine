						<div class="col-md-7">
							<p>Among the biggest challenges of doing business is having the patience to wait and watch your business grow and maximize its full potential. “I have had to constantly remind myself a million times that it takes time.  You have this big dream, vision and ambition for your brand and business. It takes time. I have studied a lot of Kenyan businesses such as <em>Darling</em> and <em>Centro Foods</em>. Some of these companies go way back, 10 or even 20 years ago. It would be unrealistic for me and you to start today and want to be where they are. Be patient, it takes time. Go through the process, the process in turn builds character.”</p>
							<p>It is important to take stock of the small wins. Remember, it’s the small picture that acts as a solid foundation when the big picture becomes blurred. “I have positioned all the good things that have happened to me in different parts of my house. They remind me of how far I have come and give me the strength to keep pushing for more. When am taking coffee, I look at one and say to myself, that happened! In my office, the bedside, literally everywhere. Our visuals are very powerful. As a Christian, I have also surrounded myself with scriptures to keep myself going.”</p>
							<p>Women have always been accused of never supporting their fellow women. Amani tells a different tale. “It’s so fantastic to see girls supporting girls. As a start up, you have your own doubts. You wonder if you are good enough a million and one times. To see Kambua order the hair and come back over and over again is very heart warming. Apart from her, many girls have supported me including Mayonde, Michelle Ntalami and my stylist J Praise. She has particularly embraced it and uses it on her clients. It has been a humbling experience.”</p>
						</div>
					</div>
					
					
					<p>As we wind up the interview, one thing is clear, this girl is all about God. She has buried herself deep in Him and allowed him to be the light on her path and guide her footsteps in whatever it is she does. She finishes by saying all you need to do is let go and let God in whatever it is you are doing; to the glory and honor of His name. She quotes the book of Ephesians 3:20-21 that says, “Now unto Him that is able to do exceedingly and abundantly above all that we ask and think, according to the power that worketh in us, unto him be glory in the church by Christ Jesus, throughout all ages, world without end. Amen.”</p>

					<center><p><strong>Amani's Nuggets of Wisdom</strong></p></center>
					<ol>
						<li>God created you to function in your purpose. You are not here to pass time. That purpose is what gives you an edge. It’s what makes you the best among the rest.</li>
						<li>God is faithful. What makes you doubt God is when you are thinking; He will not come through for you. When you are sure that He is faithful, you walk and talk in confidence, you know that He is going to come through for you.</li>
						<li>God is a loving God. He compensates for everything He has made you wait for. It comes in double portion, triple or even four times. It comes through in a bigger, better and double way. Habakkuk 2:3 says, ‘even though it may tarry, wait for it, it will happen.’</li>
						<li>God takes time to build your character so that when your blessings and portion come, they last an eternity.</li>
						<li>My Salvation is my personal journey and relationship with God. It does not matter what people say about me or how they perceive me. What’s most important to me is what God is saying about me.</li>
						<li>The bible gives me strength, hope and keeps me going. The word of God does not return to Him void. It is life and it is living.</li>
					</ol>

