						<h2>Jahmby Koikai <br /><small>Endo fighter, Endo Warrior, Endo Conqueror</small></h2>
					</header>

					<div class="row">
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>mainjune7.jpg" alt="Jamby Koikai"></center>
						</div>
						<div class="col-md-7">
							<p>It all started when she was a small girl. Nature came calling. Jahmby was no longer a little girl, according to the African tradition. Menses had come knocking, initiating her into womanhood. This is the natural process that has for over the last ten years changed the life of this reggae girl.</p>
							<p>“I remember when I was in form one and was on way to school, I felt a sudden heat wave and even threw up on the passenger sitting next to me. I got off the bus halfway and when reached home told my mom I was not feeling well. I went to the bathroom and came out screaming that I was bleeding. Bingo, my periods!”</p>
							<p>The cramps that came prior to her periods were extremely excruciating but because nobody understood what it was exactly she was suffering from, she was forced to woman up and face the horror that was her period.</p>
							<p>“My mother and my grandmother suffered from extremely painful periods so at home it was taken as normal. I did not get to give it too much thought since it was ‘normal’ as part of my family had gone through. If the pain got worse I was to take painkillers and was given the consolation that after my first child I will be okay. What everyone did not know is that a baby is not a pill.”</p>
							<p>Nobody truly understood what it was exactly that Jahmby was suffering from. The pain was getting worse by the day and the worst part of her pain was that she did not know exactly what it was that was severely eating her up in the inside.</p>
						</div>
					</div>

					<p>“Three years ago, my pain worsened. The bleeding got severe and I begun suffering from anemia. I would also cough a lot. At first I thought it was the air conditioner in the studio and when I got in I would switch it off. Later, I got a chance to accompany my grandma abroad for medication, we saw the same doctor. I did an ex ray and it revealed that for over month my lungs had collapsed.” She added.</p>
					<p>The process of treating her collapsed lungs begun and it was a tedious process. She was always on medication, treating something that kept recurring because the root cause had not been identified.</p>
					<p>“I went through my first surgery and was discharged. A month later, I had recuperated. Soon, I had to go back to the hospital, five surgeries followed. The doctors were treating what they thought was………. But it later emerged that it was Endometrium Lyceums that had punctured my diaphragm and every time I menstruated, the blood would flow to my lungs causing the collapse.”</p>

					<div class="row">
						<div class="col-md-7">
							<p>The dawn of what she truly was suffering from came about when she was doing a fundraiser for her grandmother who was suffering from cancer. A girl called her to tell her that maybe whatever she was suffering from could have been Endometriosis.</p>
							<p>“I remember sharing the story of how my lungs had collapsed five times and I had been on and off to the hospital. A girl called me and told me that maybe I might be suffering from Endometriosis and I should go get tested. I went to the Aga Khan hospital and a Dr. Patel did the laparoscopy procedure. I was diagnosed with severe pelvic endometriosis. From there hence forth I have been living with the condition and managing it.”</p>
							<p>The burden that comes with living and managing endometriosis took a toll on Jahmby. She had to learn how to breathe with pipes through her chest, and also learn how to speak and walk again but the fighter spirit in her is still here, braving on, strength after strength.</p>
							<p>“Endo comes with a price. When I am negotiating for a job position, I don’t bargain for an upward salary. Rather, I ask for a seven day off on a monthly basis to be able to stay at home and deal with my pain, heat wave and mood swings. This has coated me off so many opportunities. I was also pursuing my third degree at Daystar University but being on and off in school affected my grades and I didn’t do well. Because it’s a mutative disease, it has gotten to my heart and I am on heart medication. The disease has also strained my financial muscle since it is expensive to manage.”</p>
							
						</div>
						<div class="col-md-5"><br /><br />
							<center><img src="<?php echo base_url('images/'); ?>mainjune4.jpg" alt="Jahmby"></center>
						</div>
					</div>

					<p>The fear of the unknown is the worst, how about fearing for the certain? After every three weeks, Jahmby is always sure that a debilitating pain will keep her company till the end of that month.</p>
					<p>“It starts with a heat wave, a heat so much that you might think you are in between Mombasa and Mandera at the same time. Then there is the pain, an excruciating pain that feels like an electric power saw cutting you into two. Sometimes you lie on the floor and in so much pain other times you are rushed into the hospitals emergency unit.”</p>
					<p>Difficult times like this call for a solid support system and though some people deserted her amid her pain, there are those few who have been hell bent about riding through the Endometriosis pain with her.“My mom and my sister have been very supporting through this really trying moment. During bad days I lock myself in my room stay there all day and not talk to anyone and my mom has been very patient with me. It’s the of dream of every parent to see their child thrive on health and its been tough on her but she’s been strong and stood by my side and I love her for that.”</p>
					<p>With Endometriosis being a real challenge facing women, it is time for the government through the Ministry of Health to give reproductive health be given a serious backing. “Reproductive health is a hush topic. We do not talk about it. It’s a silent phenomenon that has many conditions affecting women, who are usually left to figure out healing on their own. By the time a woman gets to a gynecologist for diagnosis or test, it’s probably too late so we need to speak up more. The government needs to intervene. It should train more reproductive health specialists, set aside good money for research aimed to at finding cures to Endometriosis and other reproductive conditions.  In the mean time there should be a subsidized cost of treating and managing the disease.”</p>
					<p>Through the Endometriosis foundation of keys, women suffering from the disease have a common platform where they meet and talk about what they are going through. As we wind up Jahmby gives hope to any woman going through Endo pain. “You are not alone, we are together in this pain, be strong, be positive trust in God and take one step at a time.”</p>

