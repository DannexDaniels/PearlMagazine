							<p><span id="quiz">PMA: </span><b>Growing up, did you ever fathom the idea of becoming a make-up artist?</b></p>
							<p><span id="ans"> NGINA: </span>I love the question because I never thought I would ever have courage to stand in front of people and face them let alone neat their faces. I never understood why girls would wear lip-gloss. My mom would tell me that I will end up in the beauty industry and I would cry, look at me now! </p>
							<p><span id="quiz">PMA: </span><b>How did you then manage to grow as a makeup artists from the learning stage to becoming the girl that beats the faces of celebrities?</b></p>
							<p><span id="ans">NGINA: </span>My secret recipe is courage, confidence, curiosity and creativity. Since I was 14, in high school, I would do peoples make up and thereafter get punished for it…Hahaha! Through consistency and hard work I managed climb up the ladder one step at a time, improving my craft on the daily and so far I am here.</p>
							<p><span id="quiz">PMA: </span><b>In the makeup business where everyone puts their best foot forward, how have you managed to stand apart from the crowd? </b></p>
							<p><span id="ans">NGINA: </span>Everyone is a makeup artist these days. What sets you apart is you! How do you handle your client? How do you speak to them? How are your services and client care? How do you present your work, is it visible and admirable? I treat every client like they are my only client. I strive to serve them with international standards and give them an unforgettable experience.</p>
						</div>
					</div>

					<p><span id="quiz">PMA: </span><b>Let’s talk about capital. Many young people have brilliant ideas but do not know how to fund them. Where and how did you source for your startup capital?</b></p>
					<p><span id="ans">NGINA: </span>Everyone has a story of how they started out with 1000 bob, my case is different. I has no capital. I started out with eye pencil worth 20 bob. One step at a time I stocked up my make up till it was worth the name. Start where you are with what you have honey. Get going irrespective of how tough the times get. Time doesn’t wait, the more you waste it, the more you blow your money away in the wind.</p>
					<p><span id="quiz">PMA: </span><b>In terms of managing and growing the hustle or business, what advice would you give a young person starting out?</b></p>
					<p><span id="ans">NGINA: </span>When you earn set aside an amount you’ll save before you spend the cash. While saving, save with a purpose and vision. Be disciplined, it will help you through the management course of trying to build the business. Finally, allow yourself to make mistakes. Go easy on yourself when they happen, that is how we learn and grow.</p>
					<p><span id="quiz">PMA: </span><b>Who would you say are the people that have held your hand through this journey and why is it important for people in business whichever sector it is to have mentors?</b></p>
					<p><span id="ans">NGINA: </span>My mom. She has played a fundamental part of growth. She has supported me financially and even spiritually by praying for me. Another lady that stands out for me is Lucy Robi. She has been kind to me, held my hand, guided me like a big sister and shown me tough love like a boss. My siblings have also been my cheering squad giving me love and support. Mentors shield us against the market fluctuations that might surprise you and knock you off your feet and also springboard us to opportunities that will take us to the next level. The most important bit in mentorship is to learn all you can because in no time, you will be someone mentors too.</p>
