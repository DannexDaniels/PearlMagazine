					<p><span id="quiz">PMA: </span><b>We have seen Kenyan makeup brands come up by the day. In future, do you also plan to venture into makeup production for the local market?</b></p>
					<p><span id="ans">NGINA: </span>Oh yes. Currently there is work going on but I don’t want to make noise about it. I am making silent moves. The best is yet to come but just to loop you in, it won’t just be local, we are going international….</p>
					<p><span id="quiz">PMA: </span><b>Makeup trends keep changing and it is important for artists to also quickly adapt to these changes. Why is that important?</b></p>
					<p><span id="ans">NGINA: </span>Because change is the only constant thing in the world. If you don’t make an effort to be in concurrence with the changing times and trends you will be left behind. Also, these changes make work more interesting and fun. If there is change, look at it as a challenge and work towards overcoming it.</p>

					<p><span id="quiz">PMA: </span><b>How do you utilize your profit well as a young person to ensure you don’t eat into your business and create a cash crunch for yourself?</b></p>
					<p><span id="ans">NGINA: </span>Discipline yourself. You eat into your finances now, you starve your whole life. If you starve your desires right now, save and invest you’ll have more than enough in the future. Plough back your profit into the business, grow it into a network. In due time, you will reap the fruits of your hard work.</p>
					<p><span id="quiz">PMA: </span><b>Many are struggling to keep businesses afloat or take them to the next level. What would be your advice to them?</b></p>
					<p><span id="ans">NGINA: </span>You cannot know hot without knowing cold. We fall to rise. Don’t get discouraged. Try and give it a million and one shots till it works. Make try your surname. Focus on the business, specialize in it and never be lax, enough is never enough. Keep pushing, keep stretching boundaries till you have achieved your goals.</p>

