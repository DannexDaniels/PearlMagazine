						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>You run a fashion store, why did you pick the fashion industry as a sector to venture in?</b></p>
							<p><span id="ans"> CARLETTE: </span>I picked a fashion store because fashion is what I have loved since I was a kid.  I grew up surrounded by people who love fashion, especially my mother. I was always interested in the fashion stories in a magazine or newspaper. Those were the stories I would go for first. So I went for my passion, something I have always loved. </p>
							<p><span id="quiz">PMA: </span><b>Let’s talk about startup capital. How much did it cost you and how did you raise the funds?</b></p>
							<p><span id="ans">CARLETTE: </span>I would be honest, there is no specific amount of capital I started with, when I joined campus back in 2013, I used to sell jewelries to my fellow students. I started with simple earrings and necklaces. I used to hawk around campus, by the time I was finishing campus this year, I had saved a lot.</p>
						</div>
					</div>
					
					<p><span id="quiz">PMA: </span><b>Many people are torn between running an online store or renting out premises and having a physical store, what’s your take on that?</b></p>
					<p><span id="ans">CARLETTE: </span>Personally, I started with an online store; it worked perfectly for me because most people spend most of their time on the internet. So, it’s easier for them to come across your online store. I can’t say a physical store is a risk because walk-ins are done on daily basis.</p>
					<p><span id="quiz">PMA: </span><b>In relation to location, how can a business position itself to ensure it’s well placed to attract customers and still affordable yet good premise?</b></p>
					<p><span id="ans">CARLETTE: </span>First with fashion you have to be unique, and go out of your way to be a little more creative. But uniqueness is the Major Key. Simply because competition is a daily routine in the fashion world.</p>
					<p><span id="quiz">PMA: </span><b>What tools can a business use to ensure it attracts new clients on the daily?</b></p>
					<p><span id="ans">CARLETTE: </span>First you should have a good advertising of your products; good advertising is the best tool to help attract customers. Going with the trend also attracts customers a lot.</p>
					<p><span id="quiz">PMA: </span><b>Profit is key in any business. How can a business owner ensure they make daily, weekly, monthly or annual profit from their business?</b></p>
					<p><span id="ans">CARLETTE: </span>In business do not exaggerate prices. Put prices that can meet everyone’s ability. Very high prices most of the times leads up to a dead stock. Its better you get a profit of even 200 shillings rather than having dead stock.</p>
					<div class="row">
						<div class="col-md-8">
							<p><span id="quiz">PMA: </span><b>It’s the dream of every business person to expand. How do you reinvest in your business to ensure consistency and eventual growth?</b></p>
							<p><span id="ans">CARLETTE: </span>As a business person you should always be a risk taker. I always reinvest in different things that fall under fashion, thus expanding my business in lots of stuffs that make up one boutique at once.</p>
							<p><span id="quiz">PMA: </span><b>Let’s talk about paying yourself from the profit you make in your business. How do you gauge how much you should reward yourself to keep yourself motivated?</b></p>
							<p><span id="ans">CARLETTE: </span>Tell, I can say once you buy stock of worth a certain amount of money, definitely you know the amount of profit that you will get from one thing you sell and so can reward yourself with part of the profit but don’t use the capital.</p>
