					<p><span id="quiz">PMA: </span><b>How do you handle competition and ensure you stay ahead of the rest?</b></p>
					<p><span id="ans">CARLETTE: </span>I always make sure am unique, that’s what keeps me going in this industry. Uniqueness is everything in fashion.</p>
					<p><span id="quiz">PMA: </span><b>What is the future of your fashion store?</b></p>
					<p><span id="ans">CARLETTE: </span>Well, I want to create something that will be the talk of town, the likes of Vivo, Fashion Nova. I want to make my brand into something in this fashion world.</p>
					<p><span id="quiz">PMA: </span><b>You have a degree in Journalism and Mass Communication. How does that come in handy in your business?</b></p>
					<p><span id="ans">CARLETTE: </span>Yes, I hold a degree in Journalism and Mass Communication with a specialization in Public Relations and Advertising. This comes in handy in that I know how to relate with my clients, how to convince them to buy my brand as well as maintain their loyalty. I also advertise my brand well enough to people.</p>
					<p><span id="quiz">PMA: </span><b>Finally, how can people find your store and sample your products?</b></p>
					<p><span id="ans">CARLETTE: </span>My brand is called POSHBYC, the ‘’C’’ stands for my name Carlette. You can find us on various social media platforms, Instagram to be precise @poshbyc , or visit our physical store on Muindi Mbingu Street, opposite City Market, besides Tuskys ChapChap, a building called market stalls, first floor shop number A1.</p>

