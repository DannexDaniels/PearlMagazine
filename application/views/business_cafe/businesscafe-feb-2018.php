    <h2>Before you Launch That Startup</h2>
    <p><em>Story By: Rachel Nderitu</em></p>
</header>

<ol>
    <li>
        <p id="quiz">Have a clear why</p>
        <p>Before you launch that startup, ask yourself, why am I doing this? Why is this important? What need am I going to meet? Answering these questions will help you have a clear picture of your purpose and where you’re headed.</p>
    </li>
    <li>
        <p id="quiz">Do your market research</p>
        <p>Do in depth and on ground research to get a clear view of your target market. Know their patterns, their beliefs and habits. This will help you create products and services that are alluring to your consumer and will probably keep them coming for more.</p>
    </li>
    <li>
        <p id="quiz">Get the right people on the bus</p>
        <p>Get the right people on board. Make sure that your team is well positioned to carry out your startup’s mission in an effective manner. Additionally, bring in people who understand and agree with your purpose.</p>
    </li>
    <li>
        <p id="quiz">Know your source of funding</p>
        <p>Figure out how you want to fund your startup. Will you take up debt or equity or will you borrow from family and friends? If so, how much of it will you take up? Once you know how you intend to fund it, start putting in place the necessary plans to meet with and discuss with the respective people or institutions.</p>
    </li>
    <li>
        <p id="quiz">Put in effective systems and structures</p>
        <p>Ensure that you set up financial, technological, human resource, social and marketing systems to help have a seamless working and service delivery process. This will help you avoid problems in both the short term and the long term.</p>
    </li>
</ol>