            <h2>When Right Hurts!</h2>
            <p><em>Story By: Sibuor Madhako</em></p>
          </header>
          <p>When I was still in college, my ex-boyfriend who I loved so much but broke my heart came to visit me. We had general conversation in my tiny crib, seated on my bed. While leaving, we stood at the door staring at each other, then he leaned in, so close, held my face like it was a fragile diamond. So close we were that I could breathe his air and it was time to lock lips (he’s a good kisser just so you know), but I moved away and asked him to leave.</p>
          <div class="row">
            <div class="col-lg-5">
              <img src="<?php echo base_url('images/'); ?>nov-dailydose.jpg" alt="emotional cry">
            </div>
            <div class="col-lg-7">
              <p>I thought of my boyfriend, with whom they went to the same school with. I could not disrespect him like that and so I held back. When he came visiting I told him what happened and nothing was the same again. We went through rough patches and broke up. But that was not the painful bit, what hurt me deeply was that he told his friends I had slept with my ex. He even had the guts of asking who else was in the room save my ex and I and where were we seating?</p>
              <p>How dare he question my integrity? Did he know how hard it was holding myself together the push from every cell in my body which was screaming kiss him? How dare he think that just because I got intimate with him I did with every other person? Had he run out of stories to tell? Why didn’t he want to own up to his mistakes too? Instead, he threw me under the bus playing the victim card so perfectly!</p>
            </div>
          </div>
          
          
          <p>I was extremely mad. I thought to myself, you should have kissed him. Either way, didn’t we break up eventually? I was to become a woman on a mission, hunt down my ex and kiss him. I did find him, we hang out at his place but his brother was around we couldn’t kiss. So, we were to find some other time and get cozy. Along the way, carrying a lot of emotional baggage, pain and vengeance, I got fed up. I raised my hand high, slapped myself on the face and woke myself up from my nightmare.</p>
          
          
          <div class="row">
            <div class="col-lg-8">
              <p>So what if he didn’t believe that I didn’t kiss my ex, so what if he soils my image, so what if I respected him and he didn’t respect me? So what? Should I destroy myself? Hell to the no! I chose to respect our relationship and in turn respect myself but that still wasn’t enough, or too good and I got hurt. I had to find a way to go through my final year of college happy since my world had crushed right before my eyes.</p>
              <p>But again, that is life. Sometime, the right decisions hurt. It will cost you everything you valued and were trying to protect when you made that decision or took that step. What will hurt more is that you didn’t do anything wrong or at least so you will think. Dear sister girl, that is life. You will quit your job to start a business and you will drown in losses. You will try giving someone a heads up about something and they will turn on you. You will use a knife to cut the ropes that have imprisoned someone and they will use the same knife to stab you in the back. You will have sex with him, get pregnant for him, have an abortion because the ‘timing is not right’ for him to start a family, lend him money to start a business and he will use it to go pay his wife’s dowry.</p>
            </div>
            <div class="col-lg-4">
              <img src="<?php echo base_url('images/'); ?>nov-dailydose3.jpg" alt="chains of pain">
            </div>
          </div>
          <p>What then do you do? You cry, you scream at the top of your voice, you curse, you hate. You will feel like the pain is slicing your stomach into tiny pieces and you will fume blood. Don’t let that change you and consume you. Stick to being you and hold your head high. Pain comes and goes, people come and go but your conscience remains. If you ask me, it’s been over a year since we broke up, am good. Am happy that despite the fact that he did not believe me, my conscience is clear, I did not cheat on him, but most importantly I did not cheat on myself! I stood by my principles and values. So go fourth, no matter the cost, do right by you, for you!</p>

