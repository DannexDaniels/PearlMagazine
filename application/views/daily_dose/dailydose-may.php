						<h2>JANET THE JOURNEY <br /><small>Tribute to Janet Kanini</small></h2>
						<p><em>Story By : Ondiro Oganga</em></p>
					</header>
					<div class="row">
						<div class="col-lg-4">
							<center><img src="<?php echo base_url('images/'); ?>janet1.jpg" alt="Janet Kanini"></center>
						</div>
						<div class="col-lg-8">
							<br /><br />
							<p>On the first of April, we all woke up ready to prank the world. After making a big fool out of someone, we smiled and said ‘Happy April Fool’s Day!’  But, weren’t we the ones the world mocked?  Fate played a dirty game on us. Everywhere, everyone was trying so hard to prove that the news of the death of Janet Kanini Ikua was another dirty prank by Kenyans. </p>
							<p>Who would’ve thought that beneath that beautiful smile, sweet soul and warrior spirit was death encroaching slowly?  Hadn’t she been healed from cancer?  We all pondered. Though it was a really sad moment for the country, there were things about Janet that stood out that even in her death her legacy will live in our hearts forever.</p>
						</div>
					</div>					
					
					<ol>
						<li>
							<h3>Strength</h3>
							<p>She was diagnosed with stage four lung cancer at a time when her career was at peak.  Any doctor will tell you that with cancer, ‘stage four’ is a death penalty.  Janet however, chose to go through treatment and fought to live (not see) another day.  She went through her chemotherapy like a champion and finished the race.  According to her numerous TV interviews, she didn’t sink into depression and wallow in self pity.  She bravely carried on with a smile fighting and working hard towards her healing.  A testimony that she inspired the whole world with. </p>
						</li>
						<div class="row">
							<div class="col-lg-8">
								<li>
									<h3>Selflessness</h3>
									<p>After her healing, Mrs. Ikua made it her mission to raise awareness on cancer. She widely advocated for better cancer healthcare systems and advised people on how to lead healthy lifestyles.  Through her organization ‘Kanini for Africa’ and together with her husband, she travelled across the country to ensure that people get screened early and get early treatment.  She did not stay at home after her near death battle.  She could’ve spent quality time with her husband, raised her two beautiful children, got some good rest and go back to being the screen diva that she was but she didn’t . Fighting cancer and keeping people alive was her new mission. </p>
								</li>
								<li>
									<h3>Burdens = Blessing</h3>
									<p>Janet had a good TV job, a wonderful family and a loving husband. All was going according to plan for this 39 year old till cancer came knocking.  Financing her treatment milked them dry - burden.  Well wishers came together and in one fundraising she raised more than enough to cater for her treatment and not just that, she was shown love and the nation prayed for her - blessing. The chemotherapy slowly choked her crown to death as her hair begun falling off - burden.</p>
							</div>
							<div class="col-lg-4">
								<center><img src="<?php echo base_url('images/'); ?>janet.jpg" alt="Janet Kanini 2"></center>
							</div>
						</div>
							<p> Together with her kids she cut her hair and immediately transformed into a goddess in her early twenties.  She could stand in the shower and dance in the rain.  She also bid human hair weaves and wigs expense goodbye-blessing.  Finally, the cancer forced her to leave her job, quit the screens and sought medical attention - burden. After conquering cancer, God multiplied the audience she used to get on TV by a million.  Many people wanted to hear her story, loved her story, cried with her while she told her story; Most importantly got inspired by her story. </p>
						</li>
						
					</ol>
					<p>Janet’s journey is over; she is probably seated at the pinnacle of God’s service.  Praying for all our needs, smiling and watching over us…She fought a good fight and won the race. </p>
					<p><strong>NB: in Janet’s footsteps marry a man who’ll love you, adore you and stay by your side. Most importantly marry a man who has a crash on you. Some ten years ago when they featured in True Love magazine, Ikua confessed his love for her legs, and later in his eulogy he still had alot of love for her legs.</strong> </p>	
					<center><p><h4>“Even if you don’t get a miracle, you can be a miracle yourself.  Prepare yourself for the kingdom and inspire others. Do not let cancer or any other chronic disease steal your joy,”</h4> Janet Kanini Ikua.</p></center>

