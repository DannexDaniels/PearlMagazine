						<h2>AFRICAN BEAUTY</h2>
						<p><em>Story By : Ondiro Oganga</em></p>
					</header>

					<p>Well this week we are celebrating the <strong>international women's day</strong> and we wanted to celebrate with women. We engaged in a forum where individuals defined their own personal understanding of <strong>African beauty. </strong></p>
					<p>Among the key qualities that popped up now and then is the <strong>ability to accept your flaws</strong> but still confidently flaunt your flaws. No human being is perfect and beauty is attained when you accept that you are imperfectly perfect.</p>
					<p><strong>Self love</strong> was also another character that many ladies thought encompasses beauty since if you love yourself, you will be able to take good care of yourself, and treat yourself to nothing but the best.</p>
		 			<p>Finally <strong>respect for thyself</strong> was also another quality of African beauty. If one respects themselves enough, they learn to say no and walk away from anything or anyone that demeans or belittles their femininity and is able to make the right choices because of the worth attached to thyself. </p>
					<p>And just like Janet Jackson says, <strong><em>“black women possess a special indestructible strength that allows them not only to get down, but to get back up,  to get through and to get over it.”</em></strong></p>
					<p>Below are a few women whose definition of African beauty we couldn't help but share with you.</p>
