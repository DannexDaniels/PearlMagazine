						<div class="col-md-4">
							<p>Death, a stone that kills bonds resurrects unwanted ties and conflicts, a stone that kills two different birds. It lets you see for what it is, a facade clothed in religion socialism and blind loyalty, sometimes it comes down to tranquility for our Rastafarian brothers who i admired at that pount in my life.</p>
							<p>Grief can take the edge of, off anything cleric and communal. Seclusions doesn't stray off that much.</p>
							<p>Grief sends one to a quest, a quest to find what makes you tick, what makes you zone out and chill. Grief sent me to the gallows of my soul, a pandora i never dared open. Bats and shadows Lurk within, waiting, wanting to take the little sanity left.</p>
							
							<p>
						</div>
					</div>
					
					<p>My grief zoned my conscious to Rhumba. Who else can calm me except the strings and tremblements of its very own Francl Makiadi. It sent me to a single he did for grief, Kimpa kisangameni. Adage it is, but relevant.</p>
					<div class="row">
						<div class="col-md-7">
							
							<p>Grief sent me to toddlers, to the drooling young with angel eyes, little do they know what awaits them, the cruelty of humanity and insanity of gods. Innocent yet expectant, seen in the very eyes we see cuteness.  Grief transforms life into a stretch of unwoven threads. I met megan and sasha.</p>
							<p>Grief drew me to the paths of two old men, theirs seemed closer, with hearty smiles they greeted the bereaved, i thought to myself, they too knew what awaits their wake. Fond, right?</p>
							<p>Grief googles all your friends and puts them in one place. Home. I met grown women i used to to hang out with when we were toddlers, married, some single, i became open for inclusion in any conversation for an opportunity to meet an old timer. Amongst the distractions, this was the mother of all.</p>
							
						</div>
						<div class="col-md-5">
							<center><img id="hmimg" src="<?php echo base_url('images/'); ?>july_dailydose2.jpg" alt="man's hand holding a child's hand"></center>
						</div>
					</div>
							<p>I watched hands folded on my niece's chest, as a grandson to papa stood curious at the casket, touched it then walked away. Striken, wearing a similar cap, my brother walked by, a man, sobbed. I went for his shoulder and took him to his room. Thats when i got the feeling, the emptiness, not mine but what my brother felt. Sometimes we have to be strong for others. We spent the next hour in Bro's room, listening to his jams, its the only thing he could do. What we could do...i met my uncle's wife from Voi, she asked me about my campus love bird. I smiled and shrugged, she really liked her.</p>	
							<p>Wheels rolled into the distant horizon, where the earth red and green layed, to lay him home. Mood sombre interrupted with songs, some dozed off, some like i choose to occupy our minda with music , mother caught me listening to Remmy Ongala's Hit -kifo. It was long.</p>
							<p>The night drags away as locals get funky to the loud music accustomed in burials nowadays. </p>
							<p>I whisk my sleep in a hut till morning, its cold, I'm not touching that water. Don't judge me!</p>
							<p>Grief turns into denial as the clergy gives a word, words if i may say, of comfort. </p>

