            <h2>NUGGETS OF WISDOM</h2>
            <p><em>Story By: Sibuor Madhako</em></p>
          </header>
          <p>Do not regret your past. Those were decisions you made. Repeating them is what a mistake is. Let those decisions be a compass that guides you through your journey of life and help shape your destiny.</p>
          <p>The year has been long, a lot has happened. There are people we look up to, and they go through life too. Their life has ups and downs just like ours. The only difference is that they are in the limelight. That changes the game entirely. Some of them have been consumed by the fire. However, like gold, others were refined by that fire. And as a bonus, they walked out of the fire with a bucket of water to set off the fire on others. This month, let’s appreciate the nuggets of wisdom from the lives of the personalities below.</p>
          <ol>
            <li>
              <p><span id="quiz">Anjlee Gadhvi</span></p>
              <p>This is a woman that has graced our screens with class and nonchalance. We all remember when she was diagnosed with liver cancer. Her perception on life and all that entails to it changed. She learnt the art of gratitude. She stopped chasing vanity and went after the most important thing; her health. When asked what she wanted, she said it’s not a big car, but her health. She has learnt to smile upon every sunrise and sunset, live in the moment, laugh with no fear of tomorrow and love without ceasing. What advice she had for you and me? “We are trapped in a golden cage of materialism. We feel trapped but truth is we are comfortable, but we have forgotten that the gate is right there in front of us.”</p>
            </li>
            <div class="row">
              <div class="col-md-4">
                <li>
                  <p><span id="quiz">Wema Sepetu</span></p>
                  <center><img src="<?php echo base_url('images/'); ?>dec-dailydose5.jpg" alt="Wema Sepetu"></center>
                  <p>This is a girl with nine lives. Life has literally tried to bury her alive. She has been thrown to the foxes but she returns leading the pack. She has fallen to the dust a million and one times but like a phoenix, always rises. She’s the perfect replica of a girl just trying to figure out this thing called life. She has had good and bad times. She has gone through the bad with grace and the good with humility. She goes about her business and chases after her dreams. She laughs with no fear of tomorrow. She is not afraid of shedding a tear. She understands that only then will your eyes be wiped clean and the vision become clear. Lesson? Live, love laugh.</p>
                </li>
              </div>
              <div class="col-md-4">
                <li>
                  <p><span id="quiz">Somizi Mhlongo</span></p>
                  <center><img src="<?php echo base_url('images/'); ?>dec-dailydose2.jpg" alt="Somizi Mhlongo"></center>
                  <p>Somizi’s story screams that your morning comes when you wake up. It might come in your teenage years, your twenties, thirties or your forties like Somizi. This is a man that had to wait till he was forty for his breakthrough in the South African industry to come. Oh, and darling, the wait was worth it! When it finally did, the man is untouchable living his best life. He is uniquely him and has no apologies. He lives his life like its golden. People will always have an opinion about you whether you do good or bad. So, like Somizi fuck it and live your life on your own terms. Oh, and he says, he doesn’t want the American dream, he bumped into the African dream and he is addicted to it. Find yours and…</p>
                </li>
              </div>
              <div class="col-md-4">
                <li>
                  <p><span id="quiz">Omotoke Makinwa</span></p>
                  <center><img src="<?php echo base_url('images/'); ?>dec-dailydose4.jpg" alt="Omotoke Makinwa"></center>
                  <p>This girl’s story broke many hearts. After relating with her ex-husband for 14 years, her marriage to him only lasted 18 years. One day the girl goes to bed, happy that she’s living a fairytale married to the man of her dream. The next day, she wakes up only to find out her husband was expecting a child with another woman. Her whole world was crushing. However, Toke learnt that - your heartache is someone else’s hope just in case you get through it. She got through it and is now travelling the world with her book On Becoming. Lesson. “Listen when the universe speaks to you, first a pebble will hit you lightly, then you will hear a whisper but if you don’t listen, the whole building will come down crumbling. However, that’s not the worst that could happen to you, sometime good things fall out of place, for better things to fall in place.”</p>
                </li>
              </div>
            </div>
            <li>
              <p><span id="quiz">Dennis Okari</span></p>
              <p>This is pretty much self-explanatory. Keep your mouth shut. Stay grounded in your belief system and be you. Do not be moved or shaken. The storm will come and pass. You are here to stay. Will you let the storm define and ruin you? Go through the storm, grow through the storm then grow out of the storm. By all means ensure it leaves you better. And oh, just in case you feel a little consumed by the storm, go to Okari’s social media pages, there is something for everyone!</p>
            </li>
          </ol>
          

