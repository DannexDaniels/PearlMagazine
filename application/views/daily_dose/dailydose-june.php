				<p>The era of social media makes it no better. Women hide their scars on Facebook and instagram.  You powder that scar, and post up a picture. You did up the archives to find a photo of a happy family and post it up. The illusion, the fake life online keeps you going by the day while in reality you are suffering. You praise your hubby online, praying for him but in reality you wish for his death lest he will kill you soon.</p>
				<p>It’s high time just like Tonto, Mercy, Nicah and TV siren Anne Kiguta have opened up about domestic violence, we all should. We should get angry and say enough is enough. Domestic violence is real. It happens to our mothers, sisters, friends, neighbors and all sorts of people. Make it your business because if you don’t, the men will keep getting away with it and when your turn comes sister, you will not believe it. </p>

