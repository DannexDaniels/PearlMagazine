						<div class="col-md-7">
							<p>Tonto Dikeh rang the break time bell on domestic violence and since then we have seen many other African super stars particularly Nollywood stars come out and say enough is enough. They are using their voice to urge women to say no to domestic violence; using their influence to show that domestic violence knows no class and transcends geographical boundaries.</p>
							<p>When we thought things would take a different twist, the internet broke after news of a South African lady went missing and was found days later burnt to death by her boyfriend. The internet went crazy and twitter was exploding with hashtags’ like #ripkarabo. But the problem is that the internet forgets very fast. After that day, the story died. But the real horror of domestic violence is still staring at us in the eye.</p>
							<p>Growing up, the African girl has been taught that she’s the home keeper, and by force her marriage must stand. This has caused many girls, and young women get into marriage with the mentality of ‘do or die’ in the union. True to that mentality, they end up dying in this union. Tonto’s case for example, there was a time when she had to run and seek refuge at the Nigerian Embassy. She was running away from her estranged Ghanaian husband. I cannot begin to imagine the amount of humiliation she went through. Her husband literally stripped her off her dignity and self esteem.</p>
						</div>
					</div>


					<div class="row">
						<div class="col-md-4">
							<p>Another person who has literally died in her union is Mercy Aige. Posh wedding, a baby to cement the union but a temper to ensure she will die trying to hold the union together. When claims surfaced about violence years prior, she came out and defended her husband. Aren’t women taught, blanket your issues, don’t let people know? The next time it was not ‘claims’ it was bloody pictures of her and a fractured skull. Mercy was no longer Mrs. Gentry, she was a bitter Ms Aige, angry, ashamed, embarrassed. Robbed off her health, she had to undergo surgeries and she will never be the same again.</p>
							<p>Closer to home, Nicah the Queen, fiancée to top comedian and a gospel artist whose song ‘Ushuhuda’ blessed many souls broke off an engagement due to allegations of domestic violence which were thoroughly refuted by her fiance as it had been done in the previous two cases. Who would have thought, a man of ‘God’ wasn’t as forgiving and patient as the bible teaches? The lady packed her stuff, two of her children in hand and decided to reclaim her life.</p>
							<p>I wonder why many stay and suffer in silence? Maybe it’s because we prepare for the wedding and forget that when the sun sets after the big day, the reality of marriage dawns on the two. Many are not prepared let alone away of the storms that come with a marriage. Maybe it’s because of society. Women are judged on the premise of how successful her marriage is. The society takes pride in calling women a Mrs. Among peers you will be respected if married. If you are not married and you are successful now that is worse, everyone will console you by telling you that your miracle (husband) is on the way. So, for the sake of image, you will stay, cry all night and tomorrow smile and introduce yourself as Mrs so and so.</p>
