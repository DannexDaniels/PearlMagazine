					<p>2017 is here, a new year, new dreams, new plans and a new you definitely. Everyone has a big plan or better still plans for their year. Get a better paying job, move out, start a family, save more, invest wisely and be happy. These entire goals can be achieved with the right mindset and an appropriate plan but sadly many die a natural death even before the end of the first quarter of the year. According to a research conducted by Forbes magazine, 92% of people who set out new year goal do not achieve them but how about the remaining eight percent, what do they do differently to ensure that their new year goal come to fruition by the end of that year? </p>
					<div class="row">
						<div class="col-lg-4">
							<img src="<?php echo base_url('images/'); ?>sea1.jpg" alt="the sea">
						</div>
						<div class="col-lg-8">
							<h3>Make goal specific</h3>
							
							<p>Be very keen to identify what it really is that you want to achieve, break the goal down to have a clear vision of exactly what you will set out to do, for example if your goal in 2017 is to lead a healthy lifestyle, will it be achieved through healthy eating habits, working out or spiritual nourishment. Once you have identified which road among the three you will take and create a strategy of how to execute it. If it’s healthy eating habit then you consult a nutritionist come up with a diet and follow it to the latter, if it’s getting in shape through work out, enroll in a gym, know what weight you want to gain or lose.</p>
						</div>
					</div>
				
					<h3>Measure progress</h3>
					<p>Once you have begun working on your goals its good to set a timeline that will guide you on how to measure your progress. This is important because it’s a perfect reflection of how much work you have put in, where you are coming from, where you are and where you intend to go. The measure should act as motivation or challenge depending on the results you get. If they are good, then you know you are on the right track and you are doing well hence you should keep up but if the results are not satisfactory you know it’s time to change strategy or put in more work.</p>
					<h3>Be patient</h3>
					<p>Change is not easy; it’s painful in the beginning, messy in the middle but beautiful in the end. That awesome dress that everyone is drooling over took the designer days, that painting on the wall that brightens up the whole house took weeks, that bikini body you see next door took that sister months to build and thriving business has survived years of heavy tide. Go easy on yourself. </p>
					<h3>Be grateful</h3>
					<p>By the end of the season for your New Year goal, look back and appreciate how far you have come and how much you have achieved. At the end of the day something is better than nothing. You might not have abs but hello, the tyres around you waist are gone, the profit from your business may not fly you to Dubai for vacay but at least you are not worried about the landlord and maybe you might not yet be able to fast and pray and still throw curse words here and there but at least you pray, you trust and have faith in God and you are trying to walk with Him. And that guy may not have rock hard abs of cruises you around in a rolls Royce but the way he looks at you, damn sister and did you notice how much he worships the ground you step on?</p>
					<p>So this 2017 folks go out and be great!</p>

