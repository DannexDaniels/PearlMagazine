					<h3>Physiology of clinical depression</h3>
					<div class="row">
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>july_health3.jpg" alt="lonely guy"></center>
						</div>
						<div class="col-md-7">
							<p>Clinical depression consumes the possible energy the patient may have. They feel lonely, and unwanted. Suicide thoughts torment them, and as hard as one may be to control it, they may eventually take their lives. Clinical Depression may be categorized as mild, moderate or severe. If the cases of depression are periodic and repetitive, it is referred to as Recurrent Depressive Disorder. A kind of depression that displays both manic and depressive periods separated by periods of normal mood is referred to as Bipolar Affective Disorder. Mostly, specific symptoms of depression include: loss of interest or pleasure, sadness, feelings of guilt or worthlessness, disturbed sleep patterns, disturbed appetite, continued fatigue and poor attention. However, these do not mean that a person is depressed. If they extend over a period of several days, then it is a cause for alarm.  </p>
							
						</div>
						<p>A person may be unaware of the onset of clinical depression. They stick in denial and may mostly speak in ways to suggest that they are unaware of their self.  Medical practitioners believe that depression is majorly contributed to by the effects of our social environment including family, friendship and romantic relationships. It comes along due to the inability to accept and deal with these circumstances in favorable manner. It may be seen as a common and rising problem among us and in people from all social hierarchies.  </p>
					</div>
					
					<h3>Treatment of clinical depression</h3>
					<p>Treating clinical depression needs dedication and time. A number of methods and therapies may be applied to this course including lifestyle changes, social support, counseling, and nutrition, sleep and stress reduction. While these may be enough to reverse a mild case of depression, medical therapy is a crucial intervention in addition to the ones named above. In some cases, full rehabilitation and seclusion is needed in the management of post-depression patients. It is also possible to guide management of the condition through distraction activities such as physical exercise, hobbies and outdoor activities. </p>
					
