							<p>It would take Sheila*, a period of one month to find out that she was going down the drain owing to depression. When her relationship with her long-time boyfriend ended two years ago, she found it hard to get accustomed to it. She resulted to seclusion and lone time, locking herself in her room for long hours and alienating herself from the world. She would maintain her position at the desk at work all day and leave straight for home when work was done. </p>
							<p>“I am usually introverted. It was nothing new that I was quiet at work. It took my best friend almost three weeks to find out I was not well when she visited. Secluding myself made me feel content. I had found solace in my own torment. My thoughts became my safe-haven. I lost interest in all things that were fun to me before it ended.” She says. Sheila suffered bouts of adverse painful thoughts for a record four weeks. At the homestretch of it all, she was ready to take her life. “I was ready to die. It was the only solution I could come up with at that time.” She came to in the ICU ward of the Aga Khan Hospital, Nairobi, four days after she had taken an overdose of tranquillizers to take her life. </p>
