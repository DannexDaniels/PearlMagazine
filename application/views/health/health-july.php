					<h3>What is depression?</h3>
					<p>It is a period of unhappiness and low morale which lasts longer than several weeks and may include ideation of self-inflicted pain or suicide thoughts. In its mildest form, depression can mean just being in a low mood. It doesn’t stop you leading your normal life but everything seems harder to do and less worthwhile. There are various types of depression, the severest being clinical depression. </p>
					<h3>What is clinical depression?</h3>
					<p>A case of severe depression will often be described as clinical depression. The signs and symptoms of clinical depression are usually similar to those of actual depression; however, more severe. </p>
					<p>According to the World Health Organization, more than 300 million people of all ages suffer from depression globally. It has been categorized as the leading cause of disability worldwide and is a major contributor to the overall world disease burden. Depression affects the mind and in most cases, culminates in suicide. The organization states that more women than men are affected by the condition. It is possible to treat depression. In beginner symptoms, patients can be treated without medication. However, in advanced cases, patients need both talking treatments in counseling together with medication. </p> 
