							<p><strong>“Once again, we come to the holiday season. A deeply religious time that each of us observes, in his own way, by going to the mall of their choice.” - DAVE BARRY</strong> </p>
							<p>There is a call to be merry and celebrate the year that was. For most people living in the millennial age, it is common to spend a little more energy, time and money on creating a lasting memory of the festive season. However, there comes the end of the season, and problems come on with it. The true picture may be created when health center statistics in January are put into the picture. We are going to discuss this problem in this issue; so that you learn how to cheat but in a healthy way. </p>
