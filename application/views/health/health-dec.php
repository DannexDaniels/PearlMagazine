					<ol>
						<li>
							<span id="ans">ALCOHOL</span>
							<p>Alcohol is a festive branded item of consumption. People like to meet in joints during this season and indulge, sometimes taking it way overboard. Ranging from rum to wines and every other alcoholic beverage lying in between, there is need to understand its impact in the human body. Binge drinking and continued alcohol consumption brings along quite some problems and health complications. During this period, an average individual increases their alcohol consumption by quite a wide range. Both short term effects and long term effects of excessive alcohol consumption over a long period of time is not recommended. </p>
							<span id="quiz">How much alcohol is enough? </span>
							<p>The standard alcohol consumption is two bottles for an adult man and one bottle for an adult woman in a day. In the festive season, it is almost rare that people will walk around and talk while drinking. They will most probably sit around and talk. In the long run, an excessive consumption of alcohol in sitting may cause death or amnesia. This occurs since it stimulates the central and nervous system. If one consumes so much alcohol, and no activity that facilitates faster metabolism, their reasoning and judgment becomes impaired. Should this person fall or faint, they might end up choking or being unable to breathe completely. Essentially, it is recommended that someone does not take in excess of four bottles of beer in a sitting. This corresponds to 1 liter of red or white wine and 500ml of rum and other strong liquors. There is much campaign towards the excessive consumption of alcohol, so is the age limit. Alcohol has adverse effects to a still developing body. </p>
							<span id="quiz">Tip</span>
							<p>If you are going out drinking, especially in the festive season, carry a specific amount of money that will be enough for exactly the maximum amounts of alcohol you plan on taking. Do not carry your credit cards or ATM cards. Bring along someone who will guide and motion your drinking...and don’t forget the designated driver, for those who drive. (This will mostly apply to addicts).</p>
						</li>
						<li>
							<span id="ans">FOOD AND HEALTH</span>
							<div class="row">
								<div class="col-md-7">
									<p>Let’s talk food cheats. For any person who follows a healthy lifestyle and tries to keep up with the healthy food choices, the festive season brings a lot of temptation. If they especially live around fun loving people, there is possibility of them wandering from their custom feeding habits and adoption of poor diets that cause more harm to the healthy progress. </p>
									<p>The human body is formatted according to how it’s trained. For this reason, a body that does nothing but get the standard food potions, it will be a task to consume, digest and assimilate excessive and complex food components. This will lead to massive accumulation of weight as sugar is converted and stored in the adipose tissue as fat. At the end of the festive season, when sobriety resumes, the person finds themselves in a situation where all their efforts of maintaining ideal body weight have gone down the drain. Losing weight is more tasking than gaining weight for most human bodies, depending on genetic make-up. </p>
									<p>The factor of food cheats affects all individuals in spite of gender, age or race. If the person consumes excessive processed foods, sugars, and unhealthy fats in these festivities, it will cause more addictions, leading the person into unhealthy diets. It is worse if these individuals lead sedentary lifestyles. </p>
								</div>
								<div class="col-md-5">
									<img src="<?php echo base_url('images/'); ?>dec-health3.jpg" alt="festive season health tips">
								</div>						
							</div>
							<span id="quiz">Tip </span>
							<p>It is important to keep a food diary if you are on a weight loss program, especially if you are conscious of your food choices independence. A food diary is a recording of all foods an individual consumes in the span of the period they plan. At the end of the day, the person can review how much they have eaten and estimate the amount of extra calories they have consumed. It is also important to engage the body in some physical exercise. This will go a long way in burning any extra calories fetched through the day. </p>
							<span id="quiz">CAVEAT</span>
							<p><strong>An effective physical exercise session aimed at burning calories should run for a minimum of 45 minutes and a maximum of up to 3 hours according to the individual’s tolerance.</strong></p>
						</li>
