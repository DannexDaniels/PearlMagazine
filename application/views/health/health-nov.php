							<p class="align-center"><strong>“Mental health is an integral part of health. Indeed, there is no health without mental health.”</strong><br /> (World Health Organization, 2010). </p>
						</div>
						<div class="col-md-8">
							<p>In the current Kenyan political stalemate, it is coming out as crucial that we draw together conclusions that will probably unravel the mystery that is our psychological state. The discussion that trends is that everyone is mad, and the difference in severity is only categorized by degree from person to person. Do you believe in this? Does your psychological state diminish with age and life? Does what you eat influence your mental capacity? One of the most controversial practices in the current world is the concept of "what do I eat, and how does it influence my health?" One of the most prevalent yet un-discovered factors is the role of nutrition in mental health. With more cases of mental instability, stress and depression cropping up, it is necessary to look into the problem from an angle of nutrition and health. The body of knowledge that links mental health to diet choices is rapidly widening, bringing into the picture the question of how, when and where did one’s mental form get out of course. </p>
						</div>
					</div>
					
					
					<p>Early childhood and pre-conception nutrition has a great impact on the outcomes of pregnancy. Most psychiatry cases reveal a deficiency in Vitamin B6, and the omega-3 fatty acids, as well as the amino acids that are precursors to the formation of neurotransmitters. Mostly, brain formation occurs from the first trimester. Most mothers, especially young women and the uneducated ones find themselves in compromising situations, giving off to challenged offspring. This forms the mental growth from the younger age, spilling its effects into adulthood. Usually, matters of health in pregnancy and correct nutrition are looked into during prenatal visits and care. Therefore, it is important for every lady to seek immediate medical and nutritional attention when they discover that there is a bun in the oven. </p>
					<p>A compromised mental state comes about when a person’s normal psychological functions are affected by any external or internal factor. Mostly, individuals do not understand the onset of a mental disorder in early stages, as it is disguised as ‘stress’. Stress has become a household name, as everyone uses it to describe a normal feeling of low moods. However, it is important to understand that frequent prolonged feelings of ‘stress’ may be a red light for deterioration of one’s mental state. A plethora of other problems come with relation to mental health. Confusion, impaired judgment, irritability, immense mood swings and sometimes, headaches. When most people suffer an immense sad feeling, it is common that their nutritional status is affected in the process. Some may binge-eat on junk food and carbonated drinks, while others will not eat at all. </p>
					<div class="row">
						<div class="col-md-8">
							<p>It is important to make a keen selection of foods. It is cliché that we are what we eat, but it applies to our mental health. A simple meal comprised of foods from all food groups for breakfast is a better choice compared to a heavy breakfast of simple slices of bread and tea. A good breakfast guides you through the day, seeing to it that your brain has a steady supply of energy making you alert through the most crucial times of the day. Try to choose healthier snacks when stress strikes, and all your body wants is food. Mostly, a sweet or any other confectionary will do the trick. However, if the cravings are immense, try to do nothing while eating. Do not watch T.V or a movie while eating. This drives the mental capacity away from the food, and onto what you are watching. Eventually, one ends up eating too much of the snacks, and in consequence, unfavorable Body Mass Index (B.M.I) which may lead to other health complications. </p>
						</div>
						<div class="col-md-4">
							<img src="<?php echo base_url('images/'); ?>nov-health4.jpg" alt="feed you brain">
						</div>
					</div>
					
					<p>Remember that the brain is run by energy, and the brain cells never sleep. Therefore, it is important to feed the brain. Skipped meals are an injustice to the brain, and affect eventual judgment. With continued starvation, brain cells die. Unlike other somatic cells, brain cells form in pregnancy and in early life and live through a lifetime. They do not regenerate. Therefore, poor nutrition and insufficient energy supply to the brain results in reduced mental synapses, hence one ends with a reduced mental and reasoning capability. Intelligence quotient also depends on how well nourished the brain is. </p>
					<p>Take care of the brain, and the body is sorted!</p>
					
