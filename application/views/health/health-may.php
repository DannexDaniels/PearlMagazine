						<h2>NO MORE SNORE <br /><small>Kiss snoring goodbye</small></h2>
						<p>Story By: Fred Ogeti</p>
					</header>
					
					<p>Once, twice, countlessly or in a lifetime we all have had encounters with snores personally or by others. To many it is bothering and annoying with extreme ones causing fallout in relationships.</p>
					<p>Snoring is a familiar noisy breathing disorder present during body stasis i.e. sleep. It is widely common in men but stretches across the gender line and age. Sharing a bed with a snorer is intolerable and in developed countries mentioned as number one reason to divorces in the 21st century!</p>
					<p>It is caused by a lot of anatomical abnormalities and lifestyle choices. Notable causes are: aging which causes throat muscles to relax and translates to obstruction of air while breathing leading to snoring. </p>
					<center><img src="<?php echo base_url('images/'); ?>snore.jpg" alt="snoring 1"></center>
					<p>For the younger generation including infants who are not alien to this disorder, it is mostly associated with respiratory infection brought by allergies of dust and cold, abnormalities of the nose, throat and health conditions such as sleep apnea. Experts also relate obesity to snoring. Obese infants tend to snore more due to strained throat muscles.</p>
					<p>Interesting and alarming studies have been made on snoring. Monash University in Australia conducted a study on kids who snore and had higher blood pressure. The findings revealed that the subjects showed increased bad behavior and reduced intellectual ability. </p>
					<p>“It’s the first study which shows this evidence of actual brain damage”, professor Rosemary Horne says. For mothers out there, a snoring doesn’t always mean they are tired, have him checked out.</p>
					<div class="row">
						<div class="col-lg-8">
							<p>Scientists have gone out of their way to find remedies to this snoring disorder, I mean, what’s the use of funding a research if you don’t fund the proposed remedies to the findings? Breathing is an essentiality to life.</p>
							<p>Trouble shooting is done best by finding causative agents to the snoring problem. In this case, it could be anything, ranging from allergy, sleeping position, the state of your mouth while asleep and body weight. Allergies of cold can be countered with air conditioning and medication in the case of cold allergies and cleanliness will do it for dust allergies.</p>
						</div>
						<div class="col-lg-4">
							<center><img src="<?php echo base_url('images/'); ?>snore1.jpg" alt="snoring 2"></center>
						</div>
					</div>
					
					<p>However, for sleeping positions, one must first monitor ones sleeping pattern with help from a partner or a camera set to record you in sleep. This will help you know at what precise position you snore. From here you can switch positions.</p>
					<p>Snoring prompted by the arrangement of your tongue and jaw alignment is easily dealt with a mouth appliance similar to the athlete’s mouth guard to serve as air opener in the moth cavity by bringing your lower jaw and tongue forward during sleep.</p>
					<p>Lastly, for snoring related to weight, there is always more room in the gym. Get in there and work those fats out of your muscle and bid farewell to snoring and welcome the utopia of dreamland.</p>

