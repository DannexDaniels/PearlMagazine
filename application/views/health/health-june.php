					<center><h3>WAYS OF MANAGING PRIMARY DYSMENORRHEA</h3></center>

					<ol>
						<li><span id="quiz">Hormonal birth control pills</span>
							<p>Ask your physician to prescribe efficient birth control pills. These may sound off, but a low dose eases menstrual cramping in the sense that they prevent excessive production of prostaglandins. However, beware of the side effects that come with intake of the hormonal pills. These may include acne, thrombosis, breast tenderness, increased blood pressure among others. </p>
						</li>
						<li><span id="quiz">Prescription pain relievers</span>
							<p>In severe cases, it is recommended that you consult a physician who may in turn prescribe pain relievers that go a long way to therapeutically manage the cramping.</p> 
						</li>
						<li><span id="quiz">Physical activity</span>
							<div class="row">
								<div class="col-md-4">
									<center><img src="<?php echo base_url('images/'); ?>health3.jpg" alt="period pains 2"></center>
								</div>
								<div class="col-md-8">
									<p>Mild exercises ease the body and are enough to increase blood flow and stimulate release of endorphins. Avoid rigorous activities that involve lifting, strength training, cross fit among others. Instead, take light exercises in the form of simple aerobic exercises such as swimming, cycling and walking. Other light exercises include yoga poses that stretch the back, the groin, chest and abdominal muscles. These stimulate blood flow into the uterus and help to relieve pain. Physical exercise also places a probability on potential weight loss which eases menstrual cramping to a greater extent. </p>
									
									<span id="quiz">Massaging the abdomen</span>
									<p>Light strokes around the cramped areas with the fingertips in circular movements allow for increased blood flow. Repeat this process for as long as the pain eases. This is also a way of distracting yourself from the pain. </p>
									
								</div>
							</div>
							
						</li>
						<li><span id="quiz">Orgasm</span>
						<p>Sexual activity leading to an orgasm helps to relieve cramping by stimulating more blood flow into the endorphins and kill pain. Sexual intercourse is also a welcome distraction from the pain. </p>
						<p>Sexual activity before or around the cycle does not prevent menstrual cramping in any way. It is only classified under the category of physical exercise. The woman will experience menstrual cramping whether or not she is sexually active beforehand. </p>

						</li>
						<li><span id="quiz">Heat treatment</span>
						<p>Taking a hot shower or bath eases menstrual cramping. Heat prevents vasoconstriction ***and allows for more blood flow, hence easing the pain.</p>
						<p>You may also fill a bottle with hot water and place it over the abdomen and hold it in place for 20-30 minutes. </p>

						</li>
						<li><span id="quiz">Nutrition</span>
							<div class="row">
								<div class="col-md-4">
									<center><img src="<?php echo base_url('images/'); ?>health2.jpg" alt="period pains 3"></center>
								</div>
								<div class="col-md-8">
									<p>Vitamin supplements ease cramping but need to be prescribed by a trained physician. Such supplements include omega-3 fatty acids, magnesium, Vitamin B1 and B6. Avoid self-subscription. Always read the package instructions and keep the help of a doctor close.</p>
									<p>Taking supplements during the actual cramping does not effectively function. It is necessary to uptake the regime of vitamin supplementation earlier so as to maintain the body functions in the line of the vitamin effects. </p>
