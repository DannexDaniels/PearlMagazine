						<h2>HOW TO DEAL WITH PERIOD PAINS</h2>
						<p>Story By: Mwende Mati</p>
					</header>

					<p>‘Unpaid leave’, ‘flooding downsouth’‘ketchup diet’, ‘girl flu’, ‘Aunt Flo’, ‘killing the lamb’… we all have our code word for the monthly ‘tenant’ downstairs. Periods can be a messy affair; it is an understatement compared to the amount of pain they come with. While some ladies experience little to no pain at all during their menstrual flow, it is hell to others as they come with massive muscular cramping and a plethora of other symptoms. The good news is there are ways of alleviating these pains. However, one needs to understand the kind of pain they are experiencing. Menstrual cramping (dysmenorrhea) comes in different forms and may be generally categorized into two descriptions: primary dysmenorrhea and secondary dysmenorrhea. </p>

					<div class="row">
						<div class="col-md-8">
							<p>Primary dysmenorrhea is more commonly experienced. Usually, it is caused by the overproduction of prostaglandins. Prostaglandins are hormones normally produced in the cycle to ease the sloughing off of the endometrium. Production in normal levels poses no complications, but overproduction leads to menstrual cramping as a result of constricted blood flow into the uterus and the endometrium.  Secondary dysmenorrhea may be caused by underlying medical conditions such as endometriosis, pelvic inflammatory diseases, Sexually Transmitted Infections, problems with Intra Uterine Devices or fibroids. The symptoms of secondary dysmenorrhea are usually more pronounced and bring more complications. In the case of other symptoms, it is necessary for one to consult a medical professional for medical intervention. </p>
							<p>Being more complex, secondary dysmenorrhea is manageable through surgery to eliminate the consequences. In the case of pelvic inflammation, antibiotic prescriptions go a long way. </p>
	
