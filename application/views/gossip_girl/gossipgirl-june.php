							<p>We all knew Vera Sidika as the dark girl with a big derrière until photos of her, fifty shades lighter surfaced on the internet. We all went crazy, giving her names like Vera Si'jik'a but after a while we let her be and we thought we are now proud of our melanin till the real OG decided according to Octopizzo ‘kujitoa rangi'</p>
							<p>I used to like the previous OG, not so tall, dark, yes very dark, the good kind of dark that makes you want a guy and handsome, umh let’s just say lookable. The tattoos blended well with the skin, made him more gangster. His presence in his videos gave them some seriousness. For example his hit song Mazishi, hard lyrics that we barely hear nothing because he somehow lived abroad in his dreams growing up and now he’s living that dream. We see a dark guy, really dark guy and he just sets the mood for the song.</p>
							<p>Problem is unlike Vera who has no problem shouting on top of the world how costly the skin lightening, (actually, bleaching…lightening is just an overrated way of saying it), the real OG, won’t hear anything about it. According to him, dark people are broke, walk in the sun, drink dirty water and cannot afford facial scrubs! His girlfriend introduced him to scrubs; he can afford bottled sparkling water and drives around Nairobi like a boss.</p>
