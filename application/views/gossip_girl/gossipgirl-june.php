							<p>I wonder though, why are his hands darker? Is it only his face that is always chauffeured around and the rest of the body walks in the sun? The clean water he drinks is yet to trickle down to his other body parts? And the scrubs, oh by the time he’s done he’ll be giving Huddah a run for her money. He has stood his ground. It’s not bleaching…Oops skin lightening! He terms it as ‘better living.’</p>
							<p>While many of us tweeted calling him Julius Yellow, deep down we are also suffering from great insecurities. The difference between us and the OG is that he had the courage to go forth and ‘drink clean water.’ He thought and thought through it and decided irrespective of all the trolling he’d go through he will still go ahead and do his facial scrubs.</p>
