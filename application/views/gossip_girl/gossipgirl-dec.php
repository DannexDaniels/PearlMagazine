            <h2>THE ‘CAR BY DECEMBER’ PROPHECY!</h2>
            <p><em>Story By: Sibuor Madhako</em></p>
          </header>
          <div class="row">
            <div class="col-md-5">
              <div class="well">
                <center><img src="<?php echo base_url('images/'); ?>dec-gossipgirl.jpg" alt="#40daysofglory"></center>
              </div>              
            </div>
            <div class="col-md-7">
              <p><strong>We live in an age where the church is no longer a safe haven. It’s a place where our weaknesses are exploited and our emotions toyed around with for the benefit of ‘mum’ and ‘dad’. </strong></p>
              <p>It’s that time again, time we had a chat. Today, we shift our focus from the entertainment industry and March in the glory of Nairobi’s church of excellence. The other day, ‘mum’ took to tweeter to prophesize. I like prophecies don’t get it twisted. Particularly if they are prophecies that speak blessings into my life.</p>
              <p>“Before the end of December, you shall be driving your own car, in Jesus name! <strong>#40DaysOfGlory</strong>. That is what ‘mum’ tweeted. RMA motors did not hesitate. They were ready and willing to tap into that blessing. Quickly they replied, “AMEN.” </p>
              <p>Everyone wants a good life, don’t you? I mean a house in Gigiri, Nyari to be precise, with a double-parking space for a Range, a Mercedes and a couple of speed bikes. We want to wear human hair like she does, the real one, not the bargained semi human hair that is bought in river road that ends up smelling like goat when you try blow-drying it. We want good shoes like hers, an iPhone and daughters who would start families with ‘white’ men.</p>
            </div>
          </div>
          
          <p>For her, and the few belonging to the elite club, this prophecy is relevant and doable. Hayaye! People tithe in that church and give like there is no tomorrow or like bills choking life out of them. Don’t you remember sometime back mama said; “If you can’t tithe don’t come to…?” There is someone whose car is at the port, clearance <em>imekua shida</em> (has become a problem), so in forty days they believe they would have cleared and spin their wheel. How about he who is waiting for their cheque to mature to walk into a showroom and gift themselves? Good, now you understand.</p>
          <p>However, there is another group, the majority who want this prophecy but are in dire need of more practical prophecies. There is ye who has had nothing to eat for days…they need food. There is she whose marriage that is barely a year old but is crumbling faster than the Kenyan economy that needs prophecy in her marriage. How about the one that has her last exams to clear college but does not have an exam card because of school fees? Ye whose parent took a loan to educate, years after graduation the job he does is looking for a job? See, yeah good.</p>
          <p>The church is supposed to be a sanctuary. A safe place. On the contrary it has become a business class and a place people flex financial muscle. Mathew 11:28-30 says; <em>Come unto me, all ye that labored and are heavy laden, and I will give you rest. Take my Yoke upon you, and learn of me, for I am meek and lowly in heart: and ye shall find rest unto your souls. For my yoke is easy and my burden is light.</em> Dear ‘mum’ that is God speaking, but how are His people to find rest through His minister if all they have to worry is where to find the cash to buy a car to match the standards and class of your church? ‘Mum,’ some of these people are drowning in debt, they need rest. As a servant of God, prophesy that, rest. </p>
          <p>There is more to the gospel than preaching about prosperity. Mathew 6:33 says, <em>“But seek ye first the kingdom of God, and his righteousness: and all these things shall be added unto you.”</em> Dear ‘mum’ the cars will come, even in the blink of an eye but if we do as God says and seek him first. You cannot be telling a bunch of people thriving on moral filth that God will reward, no! You cannot preach blessings to people who the only verse they know in the bible is <em>Jesus wept!</em> Teach them about God, may they eat, breath and live by the Word, for the Word. Then ‘mum’ only then will such prophecies begin taking shape.</p>
          
          <div class="row">
            <div class="col-md-8">
              <p>As I wind up mama, please put the prosperity gospel on hold. Take off your expensive hair and wig, leave the comfortable house and go to the mountains, for this country is in dire need of prayer and prophecy. Pray for Omollo and Kinuthia, that they may have thee grace to treat one another as brother and sister. Pray that God gives our leaders wisdom, pray that may justice prevail in this country; that may women never have to bury their children nor men die from accidental stray bullets while out in the streets fending for their families. Pray that may God hold this country still and may He use its citizens to the glory and honor of His name.</p>
              <p><strong>PS: Lamborghini gifted Pope Francis a supercar worth 237,000 dollars but the pope auctioned it. The money will go to four charitable projects, including one aimed at rebuilding the homes and churches of persecuted Christians in the Nineveh Plains in Iraq. </strong><em>I know ‘dad’ and ‘mum’ are still in shock. HOW? They just can’t get it!</em></p>
