					<p><span id="quiz">PMA: </span><b>Talk to us about your journey of growth and development on radio?</b></p>
					<p><span id="ans">DIANA: </span>In the beginning, I had to be persistant and hungry to be on air, I had to continiously educate myself on broadcasting and public speaking. I was priviledged to have mentors who guided me and got me the right networks to comb my way through my career</p>
					<p><span id="quiz">PMA: </span><b>Radio is a powerful tool for societal transformation, how are you using it?</b></p>
					<p><span id="ans">DIANA: </span>By being honest to my audience and not faking reality, I feel that sometimes the industry is a smoking screen, people pretend to be what they are not, flashy lifestyles and parties etc. Im trying to passage a message to the young especial the girls that you dont have to please anyone to get to where you want to be or where you are going. Be authentic to yourself first.</p>
					<p><span id="quiz">PMA: </span><b>So far through radio what have you achieved as Diana?</b></p>
					<p><span id="ans">DIANA: </span>First the human connection I have made with numerous people both physically and on social media. Secondly  the awareness I have been able to create with my campaign called Smear for Smear ke on cervical cancer awareness is recieveing a lot of attention. However im still work in progress.</p>
					<p><span id="quiz">PMA: </span><b>What challenges have you faced being a radio presenter?</b></p>
					<p><span id="ans">DIANA: </span>
					I dont neccesarily see challenges as a presenter, I see opportunities for growth so I would say I havent had any challenges yet. <br />
					Oh wait! Working on holidays when I would rather be with my precious family</p>
					<p><span id="quiz">PMA: </span><b>What kind of satisfaction comes with being on radio?</b></p>
					<p><span id="ans">DIANA: </span>That I love what I do and I wake up everyday happy because I have the power to connect and tell stories plus entertain a lot of people. </p>

					<p><span id="quiz">PMA: </span><b>Because you interact with their fan base, what do you think Kenyan artists can do to bridge the gap between them and their competitors?</b></p>
					<p><span id="ans">DIANA: </span>Invest in their artistry, go on city to city tours actually country to country tours.<br /> 
					Have a team that is handling their bookings, money, social media and projections on what they want to achieve next.</p>
					<p><span id="quiz">PMA: </span><b>What has been your best moment on air?</b></p>
					<p><span id="ans">DIANA: </span>Each and every day, as long as I have an audience and a mic, im happy</p>

