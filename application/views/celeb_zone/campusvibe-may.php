						<h2>PATRICK KATIKU MAWEU</h2>
						<p>Holding on to hope</p>
					</header>
					<h4>“Hope has been the bridge between where I am and where I want to get to.”</h4>
					<p>In 2008, post –election violence had gross effects on the whole country but those who felt the pinch were those in the hospitality industry at the Coast.  My dad, who used to work in the tourism sector, lost his job because of the dwindling economic value of tourism at the coast during that time.  A tiny boy with big dreams, I was left hanging the wind.  Though I had qualified to proceed to class eight and prepare for the national examination, my parents couldn’t afford my school fees. This was a difficult moment and I felt my dreams crush right in front of me.</p>
					<div class="row">
						<div class="col-lg-5">
							<center><img src="<?php echo base_url('images/'); ?>kakitu.jpg" alt="Patrick Kakitu Maweu"></center>
						</div>
						<div class="col-lg-7">
							<p>When I thought all was lost, the embers of hope flickered my way. A well-wisher, Dickson Karani, a member of Zimlat Primary School offered me a full year scholarship. Though my dad’s situation of being on and off at work took a toll on me, when I sat for my national exams the unbelievable happened. I was the second best student in my school with a total of 348 marks. </p>
							<p>Though people were happy for me and my success, fate was mocking my destiny all over again since my parents didn’t have a stable source of income to facilitate my high school education. I was stuck for one term at home as I watched my friends carry their boxes in trousers and ties leave for school.</p>
							<p>The gods of optimism, which is the faith that leads to achievement kept me grounded as I held onto my dream of becoming a journalist one day. Mr.  Laramie, upon hearing about my predicament organized a scholarship from Sweden to see me through high school.</p>
							<p>At Emusire boys, my school fee was catered for and I knew I had to reward Mr. Laramie with good grades. Having gone out of his way to give me an intellectual future, I studied hard and when I sat for my K.C.S.E exams, I was among those selected by the government to join the University. </p>
						</div>
					</div>
					
					
					<p>While preparing for our industrial attachment, I was the only one left out.  During idle chats,  most of my classmates would confide in each other on how they had already gotten attachments in formidable media houses yet I knew no one there let alone where to start.  Despite all this pressure I never doubted even for a second that I wouldn’t do my attachment and at a good place for that matter. </p>
					<p>My dad had once picked the card of media entrepreneur Eugene who had been visiting the resort he worked in.  The first time I texted him,  he didn’t reply later on I followed up with a phone call and just like magic,  I joined a team of young journalists who took me through rigorous media training and sharpened my skills in all matters camera. </p>
					<p>I spend most of my free time in the library, reading photography books. Hopefully, one day I’ll put up my studio and slowly advance it to a world class media. </p>
					<h3>“Hope is like the sun, which, as we journey toward it, casts the shadow of our burden behind us,” Samuel Smiles.</h3>

