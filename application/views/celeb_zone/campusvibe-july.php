					<p><span id="quiz">PMA: </span><b>Hip hop is known to be murky waters, what new thing are you bringing to the industry?</b></p>
					<p><span id="ans">Liquid: </span>A number of things actually,  originality for one. My music is of tremendous semblance to kapuka music. It is Kenyan before it is international. I try to reach out to masses by rapping more in Swahili than in English because Swahili is a swifter tounge for more locals than it is not. Secondly, my flow is simply incomparable to anyone else's and I take pride in this because many of my fans love this uniqueness above all else. Save for those,  my priceless ambition, drive and resilience are a needed pinch of spice on my musical plate.</p>
					<p><span id="quiz">PMA: </span><b>How do you Perceive the Kenyan hip hop scene?</b></p>
					<p><span id="ans">Liquid: </span>I think we've been better and can get better than we are now. The  Kenyan hip hop scene is not lively. I feel our rappers sound more like American artists than Kenyan ones. More of our own African culture should be promoted in our songs in my view</p>
					<p><span id="quiz">PMA: </span><b>What can people expect from Liquid in the Future?</b></p>
					<p><span id="ans">Liquid: </span>More and more tracks, conscious music definitely, robust entertainment, a couple mixtapes are on the way and people should also watch out for some pretty hot videos soon to be released.</p>
					<p><span id="quiz">PMA: </span><b>How can your fans reach you on Social media and access your music?</b></p>
					<p><span id="ans">Liquid: </span>It's <strong>@LiquidFlows</strong> on twitter, Facebook and Instagram. <br>
					My music is at www.recordhouse.com<br>
					Word of advise to artistes and other talented students?
					<strong>Never give up! Always do you.. That simple</strong></p>

