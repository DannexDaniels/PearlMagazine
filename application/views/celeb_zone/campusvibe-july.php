						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>Who is liquid?</b></p>
							<p><span id="ans">Liquid: </span>Liquid is a kenyan hiphop artiste and a student of the university of nairobi studying B.Sc Statistics.</p>
							<p><span id="quiz">PMA: </span><b>How did your music career come about?</b></p>
							<p><span id="ans">Liquid: </span>I I've always liked music first of all, even before the liking became more for hiphop than for other genres. I took to a stounge following of hip hop just after completing high school when I began writing lyrics to available instrumentals. It is at this point that I realised that it was actually something I do.</p>
							<p><span id="quiz">PMA: </span><b>Music is an art but you are doing a mathematics course in campus, what is the bridge between the two?</b></p>
							<p><span id="ans">Liquid: </span>You know scripture has it that man cannot live on bread alone, if you know what I mean. I guess mathematics in itself is an art to me. An art of numbers. School sort of balances my sheets. Most of my friends are schoolmates who also enjoy my music so I find correlation in this interaction.</p>
						</div>
						
					</div>
					
					<p><span id="quiz">PMA: </span><b>How has the music journey been?</b></p>
					<p><span id="ans">Liquid: </span>It is never easy but then again the best things in life don't come easy. Being an up and coming musician, it has been especially difficult to make listeners continually consume what I produce. To keep up consistency is a never ending test on my part. However, I have to say that it has also been a lot of fun to learn through the process and am happy to be at this point of the process.</p>
					<p><span id="quiz">PMA: </span><b>How has the student fraternity embraced your music?</b></p>
					<p><span id="ans">Liquid: </span>I think the student population, especially in the University of Nairobi, relates with the contents of my songs because I rap about common and emergent issues affecting our day to day lives. They have shown me a lot of love and support and it feels great to know someone out there appreciates the hard work I put into my music.</p>
					<p><span id="quiz">PMA: </span><b>What are some of the challenges you have encountered?</b></p>
					<p><span id="ans">Liquid: </span>Mainly financial ones. Lacking a steady income has been an enormous impediment to me producing new music. The rate of growth is undoubtedly affected by this because consistency is always an important thing when you're in the music business. Apart from that, juggling classes and studio time also gets tricky because they both fight for my attention and I fight for both of theirs’ so sometimes I experience what I like to call a conflict of priority and I don’t enjoy it much.</p>
					<p><span id="quiz">PMA: </span><b>What are some of the achievements you've had.</b></p>
					<p><span id="ans">Liquid: </span>I've had opportunities to work with other upcoming artists such as Klin K, also from UoN who have shared a wealth of experience with me. That for me has been an achievement.</p>
					

					<div class="row">
						
						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>How has being in campus helped you in harnessing your  talent?</b></p>
							<p><span id="ans">Liquid: </span>I'd say the life around school has played the largest role in reassuring me that this route I chose to follow was viable. It took close friends' and schoolmates’ advice and motivation  to eventually delve into this industry. If these people didn’t believe in what I had to offer, I would probably not call myself a rapper today. But I do and it is for them that I do what I do.</p>
							<p><span id="quiz">PMA: </span><b>What has been the contribution of campus in your music?</b></p>
							<p><span id="ans">Liquid: </span>Campus life is a buzz! It is the epitome of youthfulness and creative brilliance. At campus is where I’ve interacted with some of the best minds there are in Kenya. The diversity brought about by the campus setting, I believe, is what enables me come up with sound material in my tracks</p>
							<p><span id="quiz">PMA: </span><b>What would you say is the importance of education for an artiste?</b></p>
							<p><span id="ans">Liquid: </span>I believe a continued intake of knowledge and information is critical to any performer. There is a beauty in learning anything. For me education has been an eye opener in many respects. I derive inspiration from new found knowledge. Also, I endeavor to educate my listeners through my songs so all of what I do revolves around education primarily. I cannot stress its importance enough.</p>
							
