					<header class="align-center">
						<h2>GABIRO</h2>
						<p>Make it or make it in Kisumu</p>
					</header>
					
					<div class="row">
						<div class="col-lg-7">
							<p><span id="quiz">PMA: </span><b>When did you begin your music career?</b></p>
							<p><span id="ans">GABIRO: </span>Professionally a couple of years ago but music has always been fleet wing in my veins. I remember in primary school when the teacher once asked what I wanted to be in future I said a rapper and here I am living the dream.</p>
							<p><span id="quiz">PMA: </span><b>Did you have the slightest of idea you'll be a musician?</b></p>
							<p><span id="ans">GABIRO: </span>Yes, I always admired my predecessors who were living their dreams. I remember the highlight of me finishing high school was that I could finally get the ample time to do music.</p>
							<p><span id="quiz">PMA: </span><b>Talk to us about your musical journey?</b></p>
							<p><span id="ans">GABIRO: </span>I am a firm believer of good music with relevant content. I released my first single in 2014 amid both financial constraints and the moral support for music. Despite being my first song, it won an award on social awareness. This harnessed space for me in the market as an artist whose music makes sense. Through the years I have grown and done various collaborations that have enjoyed massive airplay and more is yet to come.</p>
						</div>
						<div class="col-lg-5">
							<center><img src="<?php echo base_url('images/'); ?>gabiro.jpg"></center>
						</div>
					</div>
					
					<p><span id="quiz">PMA: </span><b>As an artist based in Kisumu, how has the market responded to your music?</b></p>
					<p><span id="ans">GABIRO: </span>Though one step at a time we are slowly moving away from the notion that one needs to be in Nairobi to make it, it’s still a hurdle. People barely take us seriously. There is a big challenge with regards to production. Getting booked into studio drags us behind but things have begun opening up. I am going out of my way to make music that transcends boarders and I have gotten shows outside the lake region and my music is also played in national stations.</p>
					<p><span id="quiz">PMA: </span><b>After conquering Kisumu, what next for Gabiro?</b></p>
					<p><span id="ans">GABIRO: </span>Musically I thank God that I have become a house hold name and in appreciation my fans call me ‘Kisumu President’. On the other hand I am embarking on a mission to decolonize the mindset of other artists from the ‘Nairobi perception’. My dream is to see Kisumu’s music industry blossom and the city becoming the ultimate entertainment hub.</p>
					<p><span id="quiz">PMA: </span><b>What are the challenges you face as an artist?</b></p>
					<p><span id="ans">GABIRO: </span>There are a thousand and one challenges that not just me but most of my industry colleagues face but I believe challenges make life interesting, overcoming them makes life meaningful.</p>
					<p><span id="quiz">PMA: </span><b>What's has been the highlight of your career?</b></p>
					<p><span id="ans">GABIRO: </span>I was among the artists that performed during the Diamond Platinumz concert in Kisumu. It was a big honor and a humbling experience.</p>
					<p><span id="quiz">PMA: </span><b>Which artists inspire you locally and internationally and why?</b></p>
					<p><span id="ans">GABIRO: </span>I enjoy digging deep within me and tapping into my own creativity. I read a lot and research widely so that I can in the long run get music to share with my fans. Internationally I get a lot of inspiration from Jose Chameleon who is my godfather and mentor. </p>
					<p><span id="quiz">PMA: </span><b>Which gig would you like to perform at as the epitome for your career?</b></p>
					<p><span id="ans">GABIRO: </span>I would like to perform at my own concert as the main act in my city and fill up the Moi Kisumu stadium sooner if not later.</p>
					<p><span id="quiz">PMA: </span><b>Besides music what else does Gabiro do?</b></p>
					<p><span id="ans">GABIRO: </span>I also double up as a web designer and android app developer. Anyone in business mood can holla.</p>
					<p><span id="quiz">PMA: </span><b>What does the future hold for Gabiro?</b></p>
					<p><span id="ans">GABIRO: </span>I can almost see light at the end of the tunnel so the future looks bright. </p>
					<p><span id="quiz">PMA: </span><b>Do you think it’s important for artistes to get an education?</b></p>
					<p><span id="ans">GABIRO: </span>Education births knowledge in an individual. That knowledge combined with life experiences births wisdom. These two qualities will assist an artist navigate through the world of art without getting caught and stuck in the web.</p>
					<p><span id="quiz">PMA: </span><b>Parting shot!</b></p>
					<p><span id="ans">GABIRO: </span>Content makes poor men rich, discontent makes rich men poor.</p>

