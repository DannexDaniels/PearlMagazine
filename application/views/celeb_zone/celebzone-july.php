						<h2>Ndwaru Senior<br /><small>Artist, Music Writer and a Father</small></h2>
					</header>

					<div class="row">
						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>So tell us a little about yourself</b></p>
							<p><span id="ans">Ndwaru Senior: </span>My name is Ndwaru Senior, I am from Nakuru, I am Final Year Law Student Moi University, I am also an Artist,  Music Writer and Social Media Marketer and Strategist</p>
							<p><span id="quiz">PMA: </span><b>So how did you discover the  writing and music gene in you?</b></p>
							<p><span id="ans">Ndwaru Senior: </span>Music has always been in me. I used to sing in church all the way to the main choir which lead to me writing my first song in 2008. For writing I horned my skills in school and my compositions few better by the day. After I became a father I begun writing poems from my experience.</p>
							<p><span id="quiz">PMA: </span><b>How did this hobby and talent grow into a professional art?</b></p>
							<p><span id="ans">Ndwaru Senior: </span>I had this burning passion in me to let what was in me to the world. This sparked the desire to put it out there so people can read/listen and identify with me on various issues. Every hobby must always be nurtured so it can grow into something that can actually pay you. That is how I started writing on blogs and also started my journey to recording my first album.</p>
						</div>
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>july_celebzone.jpg"></center>
						</div>
					</div>

					<p><span id="quiz">PMA: </span><b>How has the blogging and recording journey been?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>I must admit that it has not been that easy bearing in mind that I am also in school and pursuing such a demanding course. I am still working on my album while being mentored by other seasoned artists in the industry. My music should be available in the wake of 2018. On blogging I am currently rebranding to switch to a platform with a more engaging and professional outlook. I am also focusing on growing my online presence on social media platforms so I can reach more people.</p>
					<p><span id="quiz">PMA: </span><b>Speaking of online, why are you trying to navigate and conquer those waters?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>We are in the 21st Century where everything is going digital. The online and social media has taken over and is actually doing much better than the mainstream media. We can't live oblivious of this fact and with so much knowledge available out there, I feel that I must be in a position where I can set the trends and occupy a position of influence so that the content I put out there can navigate through the ever more competitive art industry with much ease.</p>

					
					
					<div class="row">
						<div class="col-md-5">
							<center><img src="<?php echo base_url('images/'); ?>july_celebzone2.jpg"></center>
						</div>
						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>What are the pros and cons of an artist setting foot online and having a digital presence?</b></p>
							<p><span id="ans">Ndwaru Senior: </span>One huge advantage that comes with having an online presence is the ability to sell your content with ease. It becomes much more easy to convince people to identify with your music and writings once they find it easy to interact with you on the digital arena. <br>
							On the flip side, an online presence denies you a chance to have a completely private life. It makes you an open book and people are always on the look out for your next move and your room for blunder is totally diminished. </p>
							<p><span id="quiz">PMA: </span><b>Earlier on you mentioned fatherhood talk to us about that.</b></p>
							<p><span id="ans">Ndwaru Senior: </span>Responsible fatherhood is something that is really close to my heart. I advocate for fathers to take up their mantle of bringing up their kids with love and affection. We are living in times where we have so many absconding fathers and this means that we have so many children growing without fathers and it's really becoming hard to raise a responsible generation. I am teaming up with some other friends of mine to kick off a campaign dubbed #SayNoToTheFatherlessGeneration later in the year.
							</p>
							
						</div>
					</div>
					<p><span id="quiz">PMA: </span><b>How does fatherhood change an individual?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>Fatherhood comes with a demand to protect, nurture and bring up. It calls one to be bring up their children with love and affection and desire to give a life that will be put them on track to cope with various challenges that come with as they grow up to adulthood. You have to make sure that you make your children comfortable and this Calls for you to be completely responsible.</p>

					<p><span id="quiz">PMA: </span><b>What are those life changing experiences you've gone through that have made you appreciate life more?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>I had a period where I fell I'll in 2013 and I had to undergo two surgeries in a period of 21 days. It gave me an opportunity to focus on life and realize that we should work when we have energy because life can change in a split of a second. One moment we can be well and lively and then the next we are weak and broken. The point is to do what is in your heart right when you think about doing it. The time that you have is right there and then, if you procrastinate you might find that your opportunity to do it lapsed a long time ago.</p>
					<p><span id="quiz">PMA: </span><b>How did you manage to get through such a tough phase of your life?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>I have such a supporting family and group of friends that came through to ensure that I pull through strongly. I also have a strong faith and I was always encouraged that God can never bring me this far and abandon me at a dead end. His plan for my life must be fulfilled and there is no way I will get out of this life without His plan for me being fulfilled.</p>

					<p><span id="quiz">PMA: </span><b>What does the future hold for you judging from all the challenges you've overcome?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>I have come out stronger and more experienced and this has molded my reactions to every situation I face. I picture a future where I will be able to stand in a position where I can give hope to generations that there is always a plan for our lives. I see such a bright star shining from afar and I feel that there is no going back in anything. All I am doing is keeping forward and staying focused.</p>
					<p><span id="quiz">PMA: </span><b>What advice would you give to a young person on the brink of loosing hope due to the pain they are enduring?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>There are many options in life and giving up is never one of them. The challenges we face in life are supposed to sharpen and refine us so we emerge bigger and better. Keep your feet on the pedal and steer towards the land of hope and completion. There are always a bright day ahead. Just keep doing what you do and don't lose your faith</p>
					<p><span id="quiz">PMA: </span><b>To budding artists, why would you advice them to work with a digital strategist?</b></p>
					<p><span id="ans">Ndwaru Senior: </span>A digital strategist will help you with knowledge on the timing and the right seasons on releasing your content and how to target the right audience for specific projects. They will also help you have the all now important online presence where likes and views have become all important.</p>
					<p><span id="quiz">PMA: </span><b>What would be your parting shot</b></p>
					<p><span id="ans">Ndwaru Senior: </span>Look within yourself and identify what you can do best. Become an expert in and focus all your energy there. Don't let the challenges you encounter along the way block your way towards the accomplishment of your vision. Above everything else, Always work with a vision and strategy, as they say, when you don't where you are going you land anywhere.</p>
