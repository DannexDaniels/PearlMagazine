<div class="row">
    <div class="col-md-7">
        <p><span id="quiz">PMA: </span><b>Kindly introduce yourself to our readers and tell us briefly what you do? ‬</b></p>
        <p><span id="ans">TEEZEH: </span>My name is Jusper Nerrie Otieno, I'm an afro pop Musician by the stage name of Teezeh. I have been a recording artist for quite some time with club banger hits out and so far so good.</p>
        <p><span id="quiz">PMA: </span><b>What genre of music do you do and what's the inspiration behind it? ‬</b></p>
        <p><span id="ans">TEEZEH: </span>I do fused afro-beat / afro-pop. It is borrowed from the culture I was born and bred in. Fortunately, it is still the culture we are living, I just have blended in with my surrounding. The genre gives me freedom to be myself and also room to experiment through the music I create. </p>
        <p><span id="quiz">PMA: </span><b>You have worked with some reputable artists like Oga Obinna. Talk to us about that? ‬</b></p>
        <p><span id="ans">TEEZEH: </span>Well that was a great project, we met over dinner at my bro's (Comedian Owago Onyiro's) place in South c. While playing my usual playlist on my phone, Obinna heard the song Demo and liked it. Next thing I remember we were in studio recording. Then we shot the video, which actually was done twice but had great success.</p>
        <p><span id="quiz">PMA: </span><b>So currently what are you working on and what are your future plans in the music industry? ‬</b></p>
        <p><span id="ans">TEEZEH: </span>I have a couple of singles am working on plus some major collaboration. As per my schedule now, after releasing the singles, I’ll kick off the recording of my album and then go on tour to promote it.</p>
