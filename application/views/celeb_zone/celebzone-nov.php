					<p><span id="quiz">PMA: </span><b>You are an artist based in Kisumu. Contrary to the belief that one has to be in Nairobi to make it, you are defying odds. Talk to us about that?</b></p>
					<p><span id="ans">NAHNA: </span>You have to lay a foundation as an artist. Kisumu is my foundation and I intend to make it solid. I am surrounded by an amazing team of managers and producers who push me to bring out the best in me. Fortunately enough, the audience at home love it and surprisingly, Nairobi has warmed up to me too.</p>
					<p><span id="quiz">PMA: </span><b>Being a woman is not easy let alone being a female in the entertainment industry. How have you managed to stay grounded yet still afloat as a female in the industry?</b></p>
					<p><span id="ans">NAHNA: </span>Nothing comes easy. Hard work pays and easy come easy go. I live by these philosophies. This way I stay grounded to myself as I cannot be easily swayed. I also respect myself and try as much as possible to be disciplined.</p>
					<p><span id="quiz">PMA: </span><b>Its award season, you have won awards before. What is your take on our local awards and which coveted award would you like to win someday?</b></p>
					<p><span id="ans">NAHNA: </span>Yes have a couple of Award from Vybez Awards I have 3, Best Female Artist 2012, 2013 and best artist of the year 2015...... And 2016 Best performing artist KTCA Awards. Awards motivate you as an artist by appreciating your work. I am hoping to be in the Afrima Awards next year and win too.</p>
					<p><span id="quiz">PMA: </span><b>Online presence is important for any artist. How are you using your online platform to grow your career?</b></p>
					<p><span id="ans">NAHNA: </span>Due to its diversified nature and wide reach, the digital world makes it easy for artists to interact with other artists, learn from them, reach out to new audiences, and even interact more with fans. I used twitter to publicize the launch of my ‘Paro’ video and we trended for days eliciting interest in those who did not know.</p>
					<p><span id="quiz">PMA: </span><b>Many artists are often accused of copying other artists and lack of originality. How have you managed to stay authentic and what's the importance of that?</b></p>
					<p><span id="ans">NAHNA: </span>I write my own music. I think it’s a way you get to share the art in you with your fans. That way you create a brand unique to you that your fans will always identify with. I am lucky my producers understand the nature of my art and are very supportive.</p>

					<p><span id="quiz">PMA: </span><b>You are known to do eccentric shows with live bands complete with dancers. How is that important to a performance?</b></p>
					<p><span id="ans">NAHNA: </span>That is the one time you get to connect with your audience at a very intimate level. Make it as memorable as possible. We have all seen artists whose live shows flopped terribly and lose the trust of their fans. Those on the other hand that did well received accolades from their fans and even got new fans.</p>
					<p><span id="quiz">PMA: </span><b>Finally, artists have always accused each other of lack of support. Do you also support this statement?</b></p>
					<p><span id="ans">NAHNA: </span>Artists understand each other better. We know the joy and pain of the industry. Supporting each other is competitive as many dim it. It is a big win for all of us. We get to interact, critic each other and grow from that.</p>

