						<h2>Nahma<br /><small>Kisumu's Song Bird</small></h2>
						<p><em>Story By: Sibuor Madhako</em></p>
					</header>

					<div class="row">
						<div class="col-md-7">
							<p><span id="quiz">PMA: </span><b>Congratulations on the launch of your new video ‘Baby Gochna’. What is the song about?</b></p>
							<p><span id="ans">NAHNA: </span>The song means baby call me. Basically it's a love song. It can relate to long distance relationships where the only form of communication is through phone calls, texts and social media platforms. </p>
							<p><span id="quiz">PMA: </span><b>You are an artiste who was previously known to sing in Luo, judging from your previous song Paro. Why the English Luo fusion this time round?</b></p>
							<p><span id="ans">NAHNA: </span>Using my native language is a form of celebrating my culture and sharing it with the world. However, I want as much as people enjoy my music to be a part of it. That is why I infused English…to reach a wider audience.</p>
							<p><span id="quiz">PMA: </span><b>The video is very creative, simple yet amazing. What was the inspiration behind the concept?</b></p>
							<p><span id="ans">NAHNA: </span>It was time to do something different from the normal storyline of video and decided to have it simple with well choreographed moves from Yawa Dancers and Stepper Wolves Dancers.</p>
