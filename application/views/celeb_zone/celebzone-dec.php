							<p><b>“Making Constructive noise on radio” </b></p>
							<p> I once heard a joke about how Radio presenters must have been noisemakers back in school. I can neither deny nor confirm this. (Smiles)
							On radio, a presenter has to be your conscience. He/she has to be bold enough to say what others hide or avoid talking about in order to promote change. Radio is about publicly changing people's lives. This is what I mean when I say I make constructive noise.</p>
							<p> <b>Did you bump into radio or has it always been your first love?</b></p>
							<p> Yes and No. I always knew I would end up doing something in communication and media, but I assumed it would be in Public Relations. At the university however, the tides began to push me towards radio. I started by experimenting as a co-host for on campus radio. Next thing I knew I was working in Tanzania on East Africa Radio and now on Urban Radio.</p>
							
