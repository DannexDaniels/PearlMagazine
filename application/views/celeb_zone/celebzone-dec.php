					<p> <b>How does one set themselves apart in vlogging?</b></p>
					<p> I can't claim to be an experienced vlogger yet but I think it all comes down to two things. One, make sure you are vlogging about something you are passionate about so that you never struggle with your content and secondly, you enjoy what you're doing. I love being a mom, I adore my husband and I consider myself fortunate to have shared my earlier life with my siblings and parents. That's why I blog about family, it's what I love the most. 
					Also, be true to who you are. Fake is a new trend many bloggers are subscribing to but trust me it's hard to maintain. Be yourself and let people fall in love with who that is. I think, as someone who watches other vlogs and reads blogs, I get attracted to people who are genuine. I feel like I can relate to them more. </p>
					<p> <b>What inspired the vlogging…</b></p>
					<p> Finding out I was pregnant gave me qualms. I tried to be the best as a mum and wife so much so that I started feeling overwhelmed and doubtful on whether I was doing a good job. Within the same period, I started interacting with so many people; friends and women who could relate in one way or the other which made me feel better. By vlogging I just thought I could create a safe haven where any new mom or wife can also laugh at their mistakes and share their experiences. </p>
					<p> <b>How I balance...</b></p>
					<p> I don't know (haha). I just try doing as much with the little free time I get. I have an amazing support system. My husband is really hands on with our girl and my nanny is heaven sent. (We really need to learn how to appreciate them more for all the work they do in our homes and with our kids.) 
					My man is also constantly pushing me to go after my dreams and pursue the things I am most passionate about. He keeps trying to create an environment where it is easy for me to balance family, business and career. I hope everyone else is blessed with a similar kind of partnership. He makes balancing seem so easy.</p>
					<p> <b>Some of the wins I've had…</b></p>
					<p> I've been fortunate to meet many people in my line of work and stand on different stages. Honestly though, having others believe in me is my greatest achievement career wise. Anyone who gave me a chance to Emcee for them or has wanted me to appear at their function, Individuals who let me organize their events, all the listeners who tune in and those who trust me enough to tell about their lives. It’s those moments when someone approaches you for help because they know you are the most qualified person they know or because they believe in what you can do. From that I have been able to get so many referrals and opportunities. </p>
					<p> <b>Challenges I've encountered...</b></p>
					<p> Everyday has new challenges. When I first started radio I remember the biggest challenge was convincing people that such a petite girl could still do a kick ass job. It was really intimidating especially when Emceeing and working with individuals who had been in the industry for years already. Right now, the only challenge I am having is carrying this 8 month old baby belly of mine around! </p>
					<p> <b>Brands</b></p>
					<p> Branding has to be a lifestyle. It's a process. It takes time and commitment. Whether you're a musician or you are growing a business. Branding requires a lot of strategic planning otherwise you're just flying blind. I think the best thing about branding though is that there are people who have done it before that we can learn from. There are also many who have made grave mistakes which we can use as reference points. I would advise young guys to identify a brand they can relate to and use that as a reference point on how to define their own.</p>

					<p> <b>The future for Myra Anubi </b></p>
					<p> (Smiles)That's the interesting part! Who knows what the future holds for any of us! It's exciting don't you think? The fact that there are so many more things we could all do or become. I think I will just grab every great opportunity that comes my way rather than limit myself to planning for a future that has no boundaries.</p>
					

